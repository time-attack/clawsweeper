export function createCachedLabelNumberLookup(fetchNumbers: (label: string) => JsonValue[]) {
  const cache = new Map<string, number[]>();
  return (label: string) => {
    const key = String(label ?? "");
    const cached = cache.get(key);
    if (cached) return [...cached];
    const numbers = uniquePositiveIntegers(fetchNumbers(key));
    cache.set(key, numbers);
    return [...numbers];
  };
}

export function createCachedIssueCommentsLookup<T = JsonValue>(
  fetchComments: (number: number) => T[],
  cache = new Map<number, T[]>(),
) {
  return (number: JsonValue): T[] => {
    const key = Number(number);
    if (!Number.isInteger(key) || key <= 0) return [];
    const cached = cache.get(key);
    if (cached) return [...cached];
    const comments = fetchComments(key);
    if (!Array.isArray(comments)) return [];
    cache.set(key, comments);
    return [...comments];
  };
}

export function createCachedIssueCommentsLookupAsync<T = JsonValue>(
  fetchComments: (number: number) => Promise<T[]>,
  cache = new Map<number, T[]>(),
) {
  const inFlight = new Map<number, Promise<T[]>>();
  return async (number: JsonValue): Promise<T[]> => {
    const key = Number(number);
    if (!Number.isInteger(key) || key <= 0) return [];
    const cached = cache.get(key);
    if (cached) return [...cached];
    const pending = inFlight.get(key);
    if (pending) return [...(await pending)];
    const next = fetchComments(key)
      .then((comments) => {
        if (!Array.isArray(comments)) return [];
        cache.set(key, comments);
        return comments;
      })
      .finally(() => {
        inFlight.delete(key);
      });
    inFlight.set(key, next);
    return [...(await next)];
  };
}

export function repairLoopPauseLabels(labels: JsonValue): string[] {
  if (!Array.isArray(labels)) return [];
  const normalized = new Set(labels.map((label) => String(label).toLowerCase()));
  return REPAIR_LOOP_PAUSE_LABELS.filter((label) => normalized.has(label.toLowerCase()));
}

export function reviewOnlyRepairLoopCompletionLabels(labels: JsonValue): string[] {
  if (!Array.isArray(labels)) return [];
  const normalized = new Set(labels.map((label) => String(label).toLowerCase()));
  const generated = normalized.has(AUTOGENERATED_LABEL);
  const autofixOnly = normalized.has(AUTOFIX_LABEL) && !normalized.has(AUTOMERGE_LABEL);
  if (!generated && !autofixOnly) return [];
  return [
    ...REPAIR_LOOP_PAUSE_LABELS,
    AUTOFIX_LABEL,
    ...(generated ? [AUTOMERGE_LABEL] : []),
  ].filter((label) => normalized.has(label.toLowerCase()));
}

export function reviewOnlyRepairLoopMissingChecks(labels: JsonValue, checks: LooseRecord): boolean {
  if (reviewOnlyRepairLoopCompletionLabels(labels).length === 0) return false;
  const gatingTotal = checks?.gatingTotal ?? checks?.total;
  return !Number.isFinite(Number(gatingTotal)) || Number(gatingTotal) <= 0;
}

export function reviewOnlyRepairLoopPendingChecks(
  labels: JsonValue,
  checks: LooseRecord,
): string[] {
  if (reviewOnlyRepairLoopCompletionLabels(labels).length === 0) return [];
  if (!Array.isArray(checks?.pending)) return [];
  return checks.pending.map((check: JsonValue) => String(check)).filter(Boolean);
}

export function reviewOnlyRepairLoopTerminalChecks(
  labels: JsonValue,
  checks: LooseRecord,
): string[] {
  if (reviewOnlyRepairLoopCompletionLabels(labels).length === 0) return [];
  if (!Array.isArray(checks?.terminalBlockers)) return [];
  return checks.terminalBlockers.map((check: JsonValue) => String(check)).filter(Boolean);
}

export function reviewOnlyRepairLoopMergeStateBlockReason(
  labels: JsonValue,
  target: LooseRecord,
): string | null {
  if (reviewOnlyRepairLoopCompletionLabels(labels).length === 0) return null;
  const mergeStateStatus = String(target.merge_state_status ?? target.mergeStateStatus ?? "")
    .trim()
    .toUpperCase();
  if (["CLEAN", "HAS_HOOKS", "UNSTABLE"].includes(mergeStateStatus)) return null;
  return `waiting for GitHub merge readiness before autofix completion: ${
    mergeStateStatus ? mergeStateStatus.toLowerCase() : "unknown"
  }`;
}

function uniquePositiveIntegers(values: JsonValue): number[] {
  if (!Array.isArray(values)) return [];
  return [
    ...new Set(
      values
        .map((value: JsonValue) => Number(value))
        .filter((number: number) => Number.isInteger(number) && number > 0),
    ),
  ];
}

