export function automergeChangelogBlockReason({ repo }: LooseRecord): string | null {
  if (String(repo ?? "").toLowerCase() !== "openclaw/openclaw") return null;
  return null;
}

export function automergeGateBlockReason(env: LooseRecord = process.env) {
  if (env.CLAWSWEEPER_ALLOW_MERGE !== "1") return "merge requires CLAWSWEEPER_ALLOW_MERGE=1";
  return "";
}

export function automergeTransientWaitConfig(env: LooseRecord = process.env) {
  const maxWaitMs = positiveInt(env.CLAWSWEEPER_AUTOMERGE_TRANSIENT_WAIT_MS, 10 * 60 * 1000);
  const intervalMs = Math.min(
    Math.max(positiveInt(env.CLAWSWEEPER_AUTOMERGE_TRANSIENT_POLL_MS, 15 * 1000), 1000),
    Math.max(maxWaitMs, 1000),
  );
  return { maxWaitMs, intervalMs };
}

function positiveInt(value: JsonValue, fallback: number) {
  if (value === undefined || value === null || value === "") return fallback;
  const parsed = Number(value);
  if (!Number.isFinite(parsed) || parsed < 0) return fallback;
  return Math.floor(parsed);
}

export function repairableCheckBlockers(checks: LooseRecord = {}) {
  return (checks.blockers ?? []).filter((blocker: JsonValue) => {
    const conclusion = String(blocker ?? "")
      .split(":")
      .pop()
      ?.trim()
      .toUpperCase();
    return conclusion ? REPAIRABLE_CHECK_BLOCKER_CONCLUSIONS.has(conclusion) : false;
  });
}

export function automergeFailedChecksRepairReason(checks: LooseRecord = {}): string | null {
  const failedCheckBlockers = repairableCheckBlockers(checks);
  if (failedCheckBlockers.length === 0) return null;
  return `current checks are failing: ${failedCheckBlockers.slice(0, 5).join(", ")}`;
}

export function automergeRebaseRepairReason(target: LooseRecord = {}): string | null {
  const mergeStateStatus = String(target.merge_state_status ?? target.mergeStateStatus ?? "")
    .trim()
    .toUpperCase();
  if (mergeStateStatus === "DIRTY")
    return "PR is behind or has merge conflicts and needs a cloud rebase repair before automerge";
  if (mergeStateStatus === "BEHIND")
    return "PR is behind the base branch and needs a cloud rebase repair before automerge";

  const mergeable = String(target.mergeable ?? "")
    .trim()
    .toUpperCase();
  if (mergeable === "CONFLICTING")
    return "PR has merge conflicts and needs a cloud rebase repair before automerge";
  return null;
}

export function automergeActivationRepairReason({
  intent,
  repo,
  title,
  files,
  target = {},
}: LooseRecord): string | null {
  const failedChecksRepairReason = automergeFailedChecksRepairReason(target.checks ?? {});
  if (failedChecksRepairReason) return failedChecksRepairReason;

  const rebaseRepairReason = automergeRebaseRepairReason(target);
  if (rebaseRepairReason) return rebaseRepairReason;

  if (String(intent ?? "") !== "automerge") return null;
  automergeChangelogBlockReason({ repo, title, files });
  return null;
}

export function automergeMergeFailureRepairReason(reason: JsonValue): string | null {
  const text = String(reason ?? "")
    .trim()
    .toLowerCase();
  if (!text) return null;
  if (text.includes("mergepullrequest") && text.includes("merge conflict")) {
    return "PR has merge conflicts and needs a cloud rebase repair before automerge";
  }
  if (text.includes("pull request has merge conflicts")) {
    return "PR has merge conflicts and needs a cloud rebase repair before automerge";
  }
  if (text.includes("merge command failed") && text.includes("merge conflict")) {
    return "PR has merge conflicts and needs a cloud rebase repair before automerge";
  }
  return null;
}

export function automergeReadinessRepairReason(reason: JsonValue): string | null {
  const text = String(reason ?? "")
    .trim()
    .toLowerCase();
  if (!text) return null;
  if (text === "mergeable state is conflicting") {
    return "PR has merge conflicts and needs a cloud rebase repair before automerge";
  }
  if (text === "merge state status is dirty") {
    return "PR is behind or has merge conflicts and needs a cloud rebase repair before automerge";
  }
  if (text === "merge state status is behind") {
    return "PR is behind the base branch and needs a cloud rebase repair before automerge";
  }
  return null;
}

export function isCanonicalLandingNeedsHumanText(value: JsonValue) {
  const text = String(value ?? "");
  if (!text) return false;
  if (/security-sensitive|needs attention|review finding|\[P[0-3]\]/i.test(text)) return false;
  return (
    /no repair lane is needed|maintainer action is to land|land .*canonical|canonical .*fix/i.test(
      text,
    ) || /active automerge candidate with no code finding/i.test(text)
  );
}

export function maintainerAutomergeOptInApprovesNeedsHuman({
  reason,
  commentCreatedAt,
  commentUpdatedAt,
  optInTime,
  replacementAutomergeRequestedBy,
}: LooseRecord) {
  if (!isCanonicalLandingNeedsHumanText(reason)) return false;
  if (replacementAutomergeRequestedBy && maintainerCredit(replacementAutomergeRequestedBy)) {
    return true;
  }
  const verdictTime = Date.parse(String(commentCreatedAt ?? commentUpdatedAt ?? ""));
  const resumeTime =
    typeof optInTime === "number" ? optInTime : Date.parse(String(optInTime ?? ""));
  return Number.isFinite(verdictTime) && Number.isFinite(resumeTime) && resumeTime > 0;
}

export function latestRepairLoopResumeTime(entries: JsonValue, command: LooseRecord) {
  if (!Array.isArray(entries)) return 0;
  let latest = 0;
  for (const entry of entries) {
    if (!entry || typeof entry !== "object") continue;
    if (entry.repo !== command.repo) continue;
    if (Number(entry.issue_number) !== Number(command.issue_number)) continue;
    if (!["autofix", "automerge"].includes(String(entry.intent ?? ""))) continue;
    if (!["executed", "waiting"].includes(String(entry.status ?? ""))) continue;
    latest = Math.max(latest, Date.parse(String(entry.comment_updated_at ?? "")) || 0);
  }
  return latest;
}

