export function planCommandAckConvergence(
  comments: LooseRecord[],
  requestedStatusMarker: string,
): { keep: LooseRecord | null; prunable: LooseRecord[] } {
  const scoped = comments.filter((comment) => {
    const statusMarker = commandStatusMarkerFromBody(comment.body);
    return !statusMarker || statusMarker === requestedStatusMarker;
  });
  const keep = selectCommandAckKeeper(scoped);
  if (!keep) return { keep: null, prunable: [] };
  const keepId = Number(keep.id ?? 0) || 0;
  return {
    keep,
    prunable: scoped.filter((comment) => {
      const id = Number(comment.id ?? 0) || 0;
      return id > 0 && id !== keepId;
    }),
  };
}

function selectCommandAckKeeper(comments: LooseRecord[]) {
  return [...comments].sort(compareCommandAckKeepPriority)[0] ?? null;
}

function compareCommandAckKeepPriority(left: LooseRecord, right: LooseRecord) {
  const leftStatus = commandAckStatusScore(left);
  const rightStatus = commandAckStatusScore(right);
  if (leftStatus !== rightStatus) return rightStatus - leftStatus;
  if (leftStatus > 0) return compareCommentsByUpdatedAtDesc(left, right);
  return compareCommentsByCreatedAt(left, right);
}

function commandAckStatusScore(comment: LooseRecord) {
  const body = String(comment.body ?? "");
  return body.includes("clawsweeper-command-status:") ||
    body.includes("<!-- clawsweeper-command-progress:start -->")
    ? 1
    : 0;
}

function compareCommentsByUpdatedAtDesc(left: LooseRecord, right: LooseRecord) {
  const leftUpdated = String(left.updated_at ?? left.created_at ?? "");
  const rightUpdated = String(right.updated_at ?? right.created_at ?? "");
  return (
    rightUpdated.localeCompare(leftUpdated) || (Number(right.id) || 0) - (Number(left.id) || 0)
  );
}

function compareCommentsByCreatedAt(left: LooseRecord, right: LooseRecord) {
  const leftCreated = String(left.created_at ?? "");
  const rightCreated = String(right.created_at ?? "");
  return (
    leftCreated.localeCompare(rightCreated) || (Number(left.id) || 0) - (Number(right.id) || 0)
  );
}

export function commandResponseMarker({ commentId, intent, headSha = "na" }: LooseRecord): string {
  return `<!-- clawsweeper-command:${commentId}:${intent}:${headSha ?? "na"} -->`;
}

export function commandResponseMarkerPrefix({ commentId, intent }: LooseRecord): string {
  return `<!-- clawsweeper-command:${commentId}:${intent}:`;
}

export function hasCommandResponseMarker(body: JsonValue, command: LooseRecord): boolean {
  const text = String(body ?? "");
  if (command.matchAnyHead) return text.includes(commandResponseMarkerPrefix(command));
  return text.includes(commandResponseMarker(command));
}

export function staleClosedItemCommandReason({
  command,
  issue,
  pull = null,
}: LooseRecord): string | null {
  const intent = String(command?.intent ?? "");
  if (!["autofix", "automerge", "re_review"].includes(intent)) return null;

  const closedAt = pull?.mergedAt ?? pull?.merged_at ?? issue?.closed_at ?? issue?.closedAt;
  if (!closedAt) return null;

  const commandTime = Date.parse(
    String(command?.comment_created_at ?? command?.comment_updated_at ?? ""),
  );
  const closedTime = Date.parse(String(closedAt));
  if (!Number.isFinite(commandTime) || !Number.isFinite(closedTime) || commandTime >= closedTime) {
    return null;
  }

  if (pull?.mergedAt || pull?.merged_at || String(pull?.state ?? "").toUpperCase() === "MERGED") {
    return `${intent} already completed after this command`;
  }
  return `PR closed after this ${intent} command`;
}

export const staleAutomergeActivationReason = staleClosedItemCommandReason;

export function repairLoopStopPauseReason({ command, entries = [] }: LooseRecord): string | null {
  const stopAt = latestRepairLoopControlTime(entries, command, ["stop"]);
  if (!stopAt) return null;

  let resumeAt = latestRepairLoopControlTime(entries, command, ["autofix", "automerge"]);
  const commandIntent = String(command?.intent ?? "");
  if (["autofix", "automerge"].includes(commandIntent) && !command?.trusted_bot) {
    resumeAt = Math.max(resumeAt, repairLoopControlTime(command));
  }

  if (stopAt <= resumeAt) return null;
  return "ClawSweeper automation was paused by a later /clawsweeper stop command";
}

function latestRepairLoopPauseTime(entries: JsonValue, command: LooseRecord) {
  if (!Array.isArray(entries)) return 0;
  let latest = 0;
  for (const entry of entries) {
    if (!entry || typeof entry !== "object") continue;
    if (entry.repo !== command?.repo) continue;
    if (Number(entry.issue_number) !== Number(command?.issue_number)) continue;
    if (!isRepairLoopPauseEntry(entry)) continue;
    latest = Math.max(latest, repairLoopControlTime(entry));
  }
  return latest;
}

function isRepairLoopPauseEntry(entry: LooseRecord) {
  if (String(entry.intent ?? "") === "stop") return true;
  if (String(entry.intent ?? "") !== "clawsweeper_needs_human") return false;
  return (entry.actions ?? []).some(
    (action: JsonValue) =>
      action.action === "label" && String(action.label ?? "") === HUMAN_REVIEW_LABEL,
  );
}

function latestRepairLoopControlTime(entries: JsonValue, command: LooseRecord, intents: string[]) {
  if (!Array.isArray(entries)) return 0;
  const intentSet = new Set(intents);
  let latest = 0;
  for (const entry of entries) {
    if (!entry || typeof entry !== "object") continue;
    if (entry.repo !== command?.repo) continue;
    if (Number(entry.issue_number) !== Number(command?.issue_number)) continue;
    if (!intentSet.has(String(entry.intent ?? ""))) continue;
    latest = Math.max(latest, repairLoopControlTime(entry));
  }
  return latest;
}

function repairLoopControlTime(entry: LooseRecord) {
  const parsed = Date.parse(String(entry?.comment_updated_at ?? entry?.comment_created_at ?? ""));
  return Number.isFinite(parsed) ? parsed : 0;
}

function inlineQuote(value: JsonValue): string {
  const text = String(value ?? "")
    .replace(/\s+/g, " ")
    .trim();
  return text
    ? `_${text.slice(0, 500)}${text.length > 500 ? "..." : ""}_`
    : "_No request text provided._";
}

