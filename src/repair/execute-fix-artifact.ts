function isBlockedFixError(error: JsonValue) {
  if (isRepairBranchPushRace(error)) return true;
  if (isRepairBranchPushBlocked(error)) return true;
  if (isRetryableCodexTransportError(String(error?.message ?? error))) return true;
  if (isCodexContextLimitError(String(error?.message ?? error))) return true;
  return /Codex produced no target repo changes|Codex \/review did not pass|Codex (?:fix worker|review-fix worker|\/review) timed out|Codex (?:fix worker|review-fix worker|\/review) failed|validation command failed|command timed out after \d+ms: git (?:fetch|push)|rebase (?:conflicts remain unresolved|produced additional conflicts)/i.test(
    String(error?.message ?? error),
  );
}

function shouldFallbackToReplacementAfterRepairError(error: JsonValue) {
  if (isRepairBranchPushRace(error)) return false;
  if (isRepairBranchPushBlocked(error)) return false;
  const message = String(error?.message ?? error);
  if (/validation command failed|Codex |no merge base/i.test(message)) return false;
  return /maintainer_can_modify=false|missing head repo\/ref|source PR #\d+ is (?:closed|merged)|permission denied|permission to [^\s]+ denied|remote rejected|could not push|repository not found|not found/i.test(
    message,
  );
}

function isExecutableFixAction(action: LooseRecord, promotedReplacement: JsonValue) {
  if (!FIX_ACTIONS.has(String(action.action ?? ""))) return false;
  if (action.status === "planned") return true;
  if (!promotedReplacement || action.status !== "blocked") return false;
  return ["fix_needed", "build_fix_artifact", "open_fix_pr"].includes(String(action.action ?? ""));
}

function executableReplacementFixArtifact(fixArtifact: LooseRecord, workerResult: JsonValue) {
  if (!shouldPromoteNeedsHumanReplacement(fixArtifact, workerResult)) return fixArtifact;
  return {
    ...fixArtifact,
    repair_strategy: "replace_uneditable_branch",
    branch_update_blockers: uniqueStrings([
      ...(fixArtifact.branch_update_blockers ?? []),
      "ClawSweeper policy: useful uneditable or unsafe source PRs are replaced with a narrow credited PR when fix execution is explicitly enabled.",
    ]),
    credit_notes: uniqueStrings([
      ...(fixArtifact.credit_notes ?? []),
      ...fixArtifact.source_prs.map(
        (source: JsonValue) => `Replacement preserves source PR credit: ${source}`,
      ),
    ]),
  };
}

function shouldPromoteNeedsHumanReplacement(fixArtifact: LooseRecord, workerResult: JsonValue) {
  if (!fixArtifact || typeof fixArtifact !== "object") return false;
  if (fixArtifact.repair_strategy !== "needs_human") return false;
  if (!Array.isArray(fixArtifact.source_prs) || fixArtifact.source_prs.length === 0) return false;
  if (
    !fixArtifact.source_prs.every(
      (source: JsonValue) => parsePullRequestUrl(source)?.repo === workerResult.repo,
    )
  )
    return false;

  const text = [
    fixArtifact.summary,
    fixArtifact.pr_body,
    ...(fixArtifact.branch_update_blockers ?? []),
    ...(fixArtifact.credit_notes ?? []),
    ...(workerResult.needs_human ?? []),
    ...(workerResult.actions ?? []).map(
      (action: JsonValue) => `${action.action ?? ""} ${action.status ?? ""} ${action.reason ?? ""}`,
    ),
  ].join("\n");
  const hasReplacementDecision =
    /replace(?:ment)?(?: PR| fix| path)?|open a replacement|cannot safely update/i.test(text);
  const hasUneditableOrUnsafeSource =
    /maintainer_can_modify\s*=\s*false|uneditable|cannot safely update|branch is unsafe|draft|mergeability unknown|checks? (?:are )?(?:skipped|failing)/i.test(
      text,
    );
  const hasBlockedFixAction = (workerResult.actions ?? []).some(
    (action: JsonValue) =>
      ["fix_needed", "build_fix_artifact", "open_fix_pr"].includes(String(action.action ?? "")) &&
      action.status === "blocked",
  );
  return hasReplacementDecision && hasUneditableOrUnsafeSource && hasBlockedFixAction;
}

function preflightRepairSourceBranchWrite(fixArtifact: LooseRecord) {
  if (fixArtifact.repair_strategy !== "repair_contributor_branch") {
    return { status: "not_applicable" };
  }
  const sourcePr = firstSourcePullRequest(fixArtifact);
  const pull = fetchPullRequest(result.repo, sourcePr.number);
  if (pull.state !== "open") {
    return {
      status: "blocked",
      source_pr: sourcePr.url,
      reason: `source PR #${sourcePr.number} is ${pull.state}`,
    };
  }
  const pauseBlock = liveRepairPauseBlock({
    pull,
    number: sourcePr.number,
    target: sourcePr.url,
  });
  if (pauseBlock) {
    return {
      status: "blocked",
      source_pr: sourcePr.url,
      reason: pauseBlock.reason,
    };
  }
  const branchBlock = sourceBranchWriteBlockReason(result.repo, pull);
  if (!branchBlock) {
    return {
      status: "writable",
      source_pr: sourcePr.url,
      head_repo: pull.head.repo.full_name,
      head_ref: pull.head.ref,
      same_repo_branch: pull.head.repo.full_name === result.repo,
      maintainer_can_modify: pull.maintainer_can_modify === true,
    };
  }
  return {
    status: "replace_uneditable_branch",
    source_pr: sourcePr.url,
    head_repo: pull.head?.repo?.full_name ?? null,
    head_ref: pull.head?.ref ?? null,
    maintainer_can_modify: pull.maintainer_can_modify === true,
    reason: `source PR #${sourcePr.number} ${branchBlock}`,
  };
}

function executeRepairBranch({ fixArtifact, targetDir }: LooseRecord) {
  const baseBranch = String(process.env.CLAWSWEEPER_FIX_BASE_BRANCH ?? DEFAULT_BASE_BRANCH);
  const sourcePr = firstSourcePullRequest(fixArtifact);
  logProgress("repairing contributor branch", { source_pr: sourcePr.url, base_branch: baseBranch });
  const pull = fetchPullRequest(result.repo, sourcePr.number);
  if (pull.state !== "open") throw new Error(`source PR #${sourcePr.number} is ${pull.state}`);
  const initialPauseBlock = liveRepairPauseBlock({
    pull,
    number: sourcePr.number,
    target: sourcePr.url,
  });
  if (initialPauseBlock) return initialPauseBlock;
  if (!pull.head?.repo?.full_name || !pull.head?.ref)
    throw new Error(`source PR #${sourcePr.number} is missing head repo/ref`);
  const sameRepoBranch = pull.head.repo.full_name === result.repo;
  const branchBlock = sourceBranchWriteBlockReason(result.repo, pull);
  if (branchBlock) throw new Error(`source PR #${sourcePr.number} ${branchBlock}`);

  const branch = safeBranchName(
    `clawsweeper-repair/repair-${result.cluster_id}-${sourcePr.number}`,
  );
  logProgress("fetching latest base for contributor repair", { base_branch: baseBranch });
  runGitNetwork(["fetch", "origin", `${baseBranch}:refs/remotes/origin/${baseBranch}`], targetDir);
  logProgress("fetching contributor PR head", {
    source_pr: sourcePr.url,
    head_repo: pull.head.repo.full_name,
    head_ref: pull.head.ref,
  });
  checkoutSourcePullRequestHead({
    targetDir,
    repo: result.repo,
    branch,
    sourcePr,
    pull,
  });
  ensureMergeBaseAvailable({ targetDir, baseBranch });
  const sourceHead = currentHead(targetDir);
  logProgress("preparing target toolchain", { source_head: sourceHead });
  prepareTargetToolchain(targetDir, currentTargetValidationOptions());
  const codexOwnsInitialRebase =
    isAutomergeRepairJob() && fixArtifact.deterministic_rebase_only !== true;
  let rebaseResult = null;
  let fastRepair: LooseRecord = {
    status: "disabled",
    reason: codexOwnsInitialRebase
      ? "initial automerge rebase is delegated to Codex repair"
      : "not evaluated",
  };
  if (codexOwnsInitialRebase) {
    logProgress("deferring initial automerge rebase to Codex repair pass", {
      branch,
      base_branch: baseBranch,
      source_head: sourceHead,
    });
  } else {
    logProgress("rebasing source branch", { branch, base_branch: baseBranch });
    rebaseResult = rebaseOntoBase({ targetDir, baseBranch });
    logProgress("source branch rebase result", {
      status: rebaseResult.status,
      previous_head: rebaseResult.previous_head,
      current_head: rebaseResult.current_head,
    });
    const mechanicalConflictResolution = tryResolveMechanicalRebaseConflicts({
      targetDir,
      rebaseResult,
    });
    if (mechanicalConflictResolution.status === "resolved") {
      logProgress("mechanically resolved rebase conflicts", mechanicalConflictResolution);
    }

    fastRepair = tryAutomergeFastRebaseRepair({
      fixArtifact,
      targetDir,
      sourceHead,
      rebaseResult,
    });
    if (fastRepair.status === "ready") {
      return pushRepairBranchAndUpdateStatus({
        fixArtifact,
        sourcePr,
        pull,
        sameRepoBranch,
        targetDir,
        sourceHead,
        prep: fastRepair.prep,
        fastRepair,
      });
    }
    if (fastRepair.status === "fallback") {
      logProgress("automerge fast rebase falling back to Codex repair path", {
        reason: fastRepair.reason,
      });
    }
  }

  const prep = editValidatePrepareMerge({
    fixArtifact,
    targetDir,
    branch,
    mode: "repair",
    baseBranch,
    fallbackReason: null,
    sourceHead,
    rebaseResult,
  });
  (prep.merge_preflight as JsonValue).target = `#${sourcePr.number}`;
  return pushRepairBranchAndUpdateStatus({
    fixArtifact,
    sourcePr,
    pull,
    sameRepoBranch,
    targetDir,
    sourceHead,
    prep,
    fastRepair,
  });
}

function pushRepairBranchAndUpdateStatus({
  fixArtifact,
  sourcePr,
  pull,
  sameRepoBranch,
  targetDir,
  sourceHead,
  prep,
  fastRepair,
}: LooseRecord) {
  const branchUpdate = branchUpdateState({ targetDir, sourceHead });
  if (dryRun) {
    return {
      action: "repair_contributor_branch",
      status: "planned",
      target: sourcePr.url,
      commit: prep.commit,
      branch_rewritten: branchUpdate.rewritten,
      merge_preflight: prep.merge_preflight,
    };
  }

  ghAuthSetupGit(targetDir);
  if (!sameRepoBranch) {
    assertRepairBranchWritable({ targetDir, pull, rewritten: branchUpdate.rewritten });
  }
  const livePull = fetchPullRequest(result.repo, sourcePr.number);
  const livePauseBlock = liveRepairPauseBlock({
    pull: livePull,
    number: sourcePr.number,
    target: sourcePr.url,
    commit: prep.commit,
    branchUpdate,
  });
  if (livePauseBlock) return livePauseBlock;
  const pushArgs = repairBranchPushArgs({ pull, rewritten: branchUpdate.rewritten });
  try {
    runGitNetwork(pushArgs, targetDir);
  } catch (error) {
    const blockedReason = repairBranchPushBlockedReason(error);
    if (blockedReason && !sameRepoBranch) {
      logProgress("repair branch push blocked; publishing prepared repair as replacement PR", {
        source_pr: sourcePr.url,
        head_repo: pull.head.repo.full_name,
        head_ref: pull.head.ref,
        reason: blockedReason,
      });
      report.actions.push({
        action: "repair_contributor_branch",
        status: "blocked",
        target: sourcePr.url,
        repair_strategy: fixArtifact.repair_strategy,
        reason: blockedReason,
        fallback: "open_fix_pr",
      });
      return openReplacementPrFromPreparedRepairCheckout({
        fixArtifact,
        sourcePr,
        targetDir,
        prep,
        fallbackReason: blockedReason,
      });
    }
    throw error;
  }
  const threadResolution = prepareReviewThreadsForMerge({
    repo: result.repo,
    number: sourcePr.number,
    targetDir,
    resolveThreads: resolveReviewThreads,
  });
  const statusCommentUpdated = updateAutomergeStatusCommentForBranchRepair({
    target: sourcePr.number,
    validationCommands: prep.merge_preflight.validation_commands,
    commit: prep.commit,
    fastRepair,
  });
  if (!statusCommentUpdated) {
    const comment = repairContributorBranchComment({
      sourcePrUrl: sourcePr.url,
      validationCommands: prep.merge_preflight.validation_commands,
      provenance: externalMessageProvenance({
        model,
        reasoning: codexReasoningEffort,
        reviewedSha: prep.commit,
      }),
    });
    run(
      "gh",
      ["pr", "comment", String(sourcePr.number), "--repo", result.repo, "--body", comment],
      {
        cwd: targetDir,
        env: ghEnv(),
        timeoutMs: currentNetworkCommandTimeoutMs(),
      },
    );
  }
  const shepherd = waitForAutomergeAfterBranchRepair({
    target: sourcePr.number,
    commit: prep.commit,
  });
  return {
    action: "repair_contributor_branch",
    status: "pushed",
    target: sourcePr.url,
    head_repo: pull.head.repo.full_name,
    head_ref: pull.head.ref,
    branch_rewritten: branchUpdate.rewritten,
    commit: prep.commit,
    fast_rebase: fastRepair?.status === "ready" ? fastRepair : null,
    automerge_shepherd: shepherd,
    status_comment_updated: statusCommentUpdated,
    merge_preflight: prep.merge_preflight,
    review_threads: threadResolution,
  };
}

function openReplacementPrFromPreparedRepairCheckout({
  fixArtifact,
  sourcePr,
  targetDir,
  prep,
  fallbackReason,
}: LooseRecord) {
  const baseBranch = String(process.env.CLAWSWEEPER_FIX_BASE_BRANCH ?? DEFAULT_BASE_BRANCH);
  const contributorCredits = sourceContributorCredits({
    fixArtifact,
    targetDir,
    repo: result.repo,
  });
  const mergedSource = mergedReplacementSourcePr({ fixArtifact, sourcePr, targetDir });
  const branch = replacementBranchName(result.cluster_id);
  const areaCapacityBlock = validateActivePrAreaCapacity({
    fixArtifact,
    targetDir,
    branch,
    repo: result.repo,
    maxActivePrsPerArea,
  });
  if (areaCapacityBlock) {
    return {
      action: "open_fix_pr",
      status: "blocked",
      branch,
      repair_strategy: "replace_uneditable_branch",
      fallback_from: "repair_contributor_branch",
      fallback_source_pr: sourcePr.url,
      fallback_reason: fallbackReason,
      ...areaCapacityBlock,
    };
  }

  ghAuthSetupGit(targetDir);
  run("git", ["checkout", "-B", branch], { cwd: targetDir });
  const mergedSourceSkip = skipMergedSourceReplacementWithoutDiff({
    mergedSource,
    targetDir,
    baseBranch,
    branch,
    fallbackReason,
    sourcePr,
    prep,
    contributorCredits,
  });
  if (mergedSourceSkip) return mergedSourceSkip;
  if (!branchHasBaseDiff({ targetDir, baseBranch })) {
    logProgress("prepared replacement branch has no changes versus base; skipping PR create", {
      branch,
      base_branch: baseBranch,
      commit: prep.commit,
    });
    return {
      action: "open_fix_pr",
      status: "skipped",
      branch,
      repair_strategy: "replace_uneditable_branch",
      fallback_from: "repair_contributor_branch",
      fallback_source_pr: sourcePr.url,
      fallback_reason: fallbackReason,
      commit: prep.commit,
      checkpoint_commits: prep.checkpoint_commits,
      merge_preflight: prep.merge_preflight,
      supersede_sources: [],
      contributor_credit: contributorCredits.map(publicContributorCredit),
      reason: "prepared replacement branch has no changes versus base after repair",
    };
  }

  pushRecoverableBranch({ targetDir, branch });
  const provenance = externalMessageProvenance({
    model,
    reasoning: codexReasoningEffort,
    reviewedSha: prep.commit,
  });
  const body = replacementPrBody({
    fixArtifact,
    fallbackReason,
    clusterId: result.cluster_id,
    provenance,
    contributorCredits,
    maintainerAttribution: jobMaintainerAttribution(),
    sourceClosingReferences: sourceClosingReferences({
      fixArtifact,
      targetDir,
      repo: result.repo,
    }),
  });
  const bodyPath = path.join(workRoot, "replacement-pr-body.md");
  fs.writeFileSync(bodyPath, body);
  const prUrl =
    findOpenPullRequestForBranch(branch, targetDir) ||
    run(
      "gh",
      [
        "pr",
        "create",
        "--repo",
        result.repo,
        "--base",
        baseBranch,
        "--head",
        branch,
        "--title",
        fixArtifact.pr_title,
        "--body-file",
        bodyPath,
      ],
      { cwd: targetDir, env: ghEnv(), timeoutMs: currentNetworkCommandTimeoutMs() },
    ).trim();
  const prNumber = pullRequestNumberFromUrl(prUrl);
  if (prNumber) ensurePullRequestOpen({ number: prNumber, targetDir });
  if (prNumber) labelReplacementPullRequest({ number: prNumber, targetDir, fixArtifact });
  if (prNumber) (prep.merge_preflight as JsonValue).target = `#${prNumber}`;
  const threadResolution = prNumber
    ? prepareReviewThreadsForMerge({
        repo: result.repo,
        number: prNumber,
        targetDir,
        resolveThreads: resolveReviewThreads,
      })
    : { status: "blocked", reason: "replacement PR URL did not include a PR number" };

  const supersededSources = supersededReplacementSources({ fixArtifact, repo: result.repo }).filter(
    (source: JsonValue) => pullRequestNumberFromUrl(source) !== prNumber,
  );
  const supersededSourceActions: JsonValue[] = [];
  for (const source of supersededSources) {
    const parsed = parsePullRequestUrl(source);
    if (!parsed || parsed.repo !== result.repo) continue;
    supersededSourceActions.push(
      closeSupersededSourcePrs
        ? closeSupersededSourcePr({
            source,
            parsed,
            replacementPrUrl: prUrl,
            targetDir,
            contributorCredits,
            provenance,
          })
        : linkReplacementSourcePr({
            source,
            parsed,
            replacementPrUrl: prUrl,
            targetDir,
            provenance,
          }),
    );
  }

  return {
    action: "open_fix_pr",
    status: "opened",
    pr_url: prUrl,
    branch,
    repair_strategy: "replace_uneditable_branch",
    fallback_from: "repair_contributor_branch",
    fallback_source_pr: sourcePr.url,
    fallback_reason: fallbackReason,
    commit: prep.commit,
    checkpoint_commits: prep.checkpoint_commits,
    merge_preflight: prep.merge_preflight,
    review_threads: threadResolution,
    superseded_sources: supersededSources,
    superseded_source_actions: supersededSourceActions,
    contributor_credit: contributorCredits.map(publicContributorCredit),
  };
}

function liveRepairPauseBlock({
  pull,
  number,
  target,
  commit = null,
  branchUpdate = null,
}: LooseRecord) {
  const pauseLabel = repairPauseLabel(pull?.labels);
  if (!pauseLabel) return null;
  return {
    action: "repair_contributor_branch",
    status: "blocked",
    target,
    commit,
    branch_rewritten: branchUpdate?.rewritten ?? null,
    paused_label: pauseLabel,
    reason: `source PR #${number} is paused by ${pauseLabel}; refusing to mutate the PR branch`,
  };
}

function tryAutomergeFastRebaseRepair({
  fixArtifact,
  targetDir,
  sourceHead,
  rebaseResult,
}: LooseRecord) {
  if (
    !canUseAutomergeFastRebase({
      isAutomergeRepair: isAutomergeRepairJob(),
      repairStrategy: fixArtifact.repair_strategy,
      fixArtifact,
    })
  ) {
    return { status: "disabled", reason: "not an adopted automerge branch repair" };
  }

  let commit = "";
  let detail = "";
  if (
    rebaseResult?.status === "rebased" &&
    rebaseResult.previous_head !== rebaseResult.current_head
  ) {
    commit = String(rebaseResult.current_head);
    detail = "clean deterministic rebase";
  } else if (rebaseResult?.status === "conflicts" && unmergedPaths(targetDir).length === 0) {
    const completed = completeMechanicallyResolvedRebase({ targetDir });
    if (completed.status !== "continued") {
      return { status: "fallback", reason: "mechanically resolved rebase did not continue" };
    }
    commit = completed.current_head;
    detail = "mechanically resolved deterministic rebase";
  } else {
    return {
      status: "fallback",
      reason: `rebase status ${rebaseResult?.status ?? "unknown"} is not fast-pathable`,
    };
  }

  if (!commit || commit === sourceHead) {
    return { status: "fallback", reason: "deterministic rebase produced no branch change" };
  }
  if (run("git", ["status", "--porcelain"], { cwd: targetDir }).trim()) {
    return { status: "fallback", reason: "deterministic rebase left working tree changes" };
  }

  logProgress("automerge deterministic rebase ready; skipping Codex fix and local review pass", {
    source_head: sourceHead,
    commit,
    detail,
  });
  return {
    status: "ready",
    reason: detail,
    source_head: sourceHead,
    commit,
    prep: {
      commit,
      checkpoint_commits: [],
      merge_preflight: {
        target: null,
        security_status: "deferred",
        security_evidence: [
          "Deterministic automerge rebase changed only base ancestry; exact-head ClawSweeper review is dispatched after push.",
        ],
        comments_status: "deferred",
        comments_evidence: ["Fresh exact-head review gates merge after the rebase push."],
        bot_comments_status: "deferred",
        bot_comments_evidence: ["Fresh exact-head review gates merge after the rebase push."],
        codex_review: {
          command: "/review",
          status: "pending",
          findings_addressed: false,
          evidence: [
            "Skipped Codex fix worker for deterministic rebase; review worker runs after push.",
          ],
        },
        validation_commands: ["GitHub checks and exact-head ClawSweeper review gate this rebase"],
        final_base_sync: {
          status: "deterministic_rebase",
          previous_head: rebaseResult.previous_head,
          current_head: commit,
          base_sha: rebaseResult.base_sha,
        },
      },
    },
  };
}

function completeMechanicallyResolvedRebase({ targetDir }: { targetDir: string }) {
  for (let attempt = 1; attempt <= 6; attempt += 1) {
    try {
      return completeRebaseIfResolved({ targetDir });
    } catch (error) {
      const paths = unmergedPaths(targetDir);
      if (paths.length === 0) throw error;
      const current = currentHead(targetDir);
      const resolved = tryResolveMechanicalRebaseConflicts({
        targetDir,
        rebaseResult: {
          status: "conflicts",
          base_ref: "",
          base_sha: "",
          previous_head: current,
          current_head: current,
          detail: String((error as Error).message ?? error),
        },
      });
      if (resolved.status !== "resolved") throw error;
      logProgress("mechanically resolved additional rebase conflicts", {
        attempt,
        paths: resolved.paths,
        reason: resolved.reason,
      });
    }
  }
  throw new Error("mechanical rebase did not complete after resolving repeated conflicts");
}

function branchUpdateState({ targetDir, sourceHead }: LooseRecord) {
  const rewritten =
    /^[0-9a-f]{40}$/i.test(String(sourceHead ?? "")) &&
    !isAncestor({ targetDir, ancestor: sourceHead, descendant: "HEAD" });
  return { rewritten };
}

function repairBranchPushArgs({ pull, rewritten }: LooseRecord) {
  const remote = `https://github.com/${pull.head.repo.full_name}.git`;
  if (!rewritten) return ["push", remote, `HEAD:${pull.head.ref}`];
  const headSha = String(pull.head?.sha ?? "");
  if (!/^[0-9a-f]{40}$/i.test(headSha)) {
    throw new Error(
      `cannot force-with-lease repair branch ${pull.head.ref}: source head sha is missing`,
    );
  }
  return [
    "push",
    `--force-with-lease=refs/heads/${pull.head.ref}:${headSha}`,
    remote,
    `HEAD:${pull.head.ref}`,
  ];
}

function assertRepairBranchWritable({ targetDir, pull, rewritten }: LooseRecord) {
  const args = repairBranchPushArgs({ pull, rewritten });
  runGitNetwork(["push", "--dry-run", ...args.slice(1)], targetDir);
}

