function executeReplacementBranch({
  fixArtifact,
  targetDir,
  supersedeSources,
  fallbackReason,
}: LooseRecord) {
  const baseBranch = String(process.env.CLAWSWEEPER_FIX_BASE_BRANCH ?? DEFAULT_BASE_BRANCH);
  const contributorCredits = sourceContributorCredits({
    fixArtifact,
    targetDir,
    repo: result.repo,
  });
  const mergedSource = mergedReplacementSourcePr({ fixArtifact, targetDir });
  const branch = replacementBranchName(result.cluster_id);
  const areaCapacityBlock = validateActivePrAreaCapacity({
    fixArtifact,
    targetDir,
    branch,
    repo: result.repo,
    maxActivePrsPerArea,
  });
  if (areaCapacityBlock) {
    return {
      action: "open_fix_pr",
      status: "blocked",
      branch,
      repair_strategy: fixArtifact.repair_strategy,
      ...areaCapacityBlock,
    };
  }
  runGitNetwork(["fetch", "origin", baseBranch], targetDir);
  const branchState = checkoutRecoverableReplacementBranch({
    targetDir,
    branch,
    baseBranch,
    fixArtifact,
  });
  prepareTargetToolchain(targetDir, currentTargetValidationOptions());
  const rebaseResult = rebaseOntoBase({ targetDir, baseBranch });
  const mechanicalConflictResolution = tryResolveMechanicalRebaseConflicts({
    targetDir,
    rebaseResult,
  });
  if (mechanicalConflictResolution.status === "resolved") {
    logProgress("mechanically resolved replacement rebase conflicts", mechanicalConflictResolution);
  }

  if (!dryRun) ghAuthSetupGit(targetDir);
  const prep = editValidatePrepareMerge({
    fixArtifact,
    targetDir,
    branch,
    mode: "replacement",
    fallbackReason,
    baseBranch,
    contributorCredits,
    allowExistingChanges: branchState.resumed && branchHasBaseDiff({ targetDir, baseBranch }),
    reconcileWithBase: branchState.resumed,
    pushCheckpoint: dryRun ? null : () => pushRecoverableBranch({ targetDir, branch }),
    rebaseResult,
  });
  const provenance = externalMessageProvenance({
    model,
    reasoning: codexReasoningEffort,
    reviewedSha: prep.commit,
  });
  const body = replacementPrBody({
    fixArtifact,
    fallbackReason,
    clusterId: result.cluster_id,
    provenance,
    contributorCredits,
    maintainerAttribution: jobMaintainerAttribution(),
    sourceClosingReferences: sourceClosingReferences({
      fixArtifact,
      targetDir,
      repo: result.repo,
    }),
  });
  if (dryRun) {
    return {
      action: "open_fix_pr",
      status: "planned",
      branch,
      resumed_branch: branchState.resumed,
      commit: prep.commit,
      checkpoint_commits: prep.checkpoint_commits,
      merge_preflight: prep.merge_preflight,
      supersede_sources: supersedeSources ? (fixArtifact.source_prs ?? []) : [],
      contributor_credit: contributorCredits.map(publicContributorCredit),
    };
  }

  if (!branchHasBaseDiff({ targetDir, baseBranch })) {
    const mergedSourceSkip = skipMergedSourceReplacementWithoutDiff({
      mergedSource,
      targetDir,
      baseBranch,
      branch,
      prep,
      contributorCredits,
      resumedBranch: branchState.resumed,
    });
    if (mergedSourceSkip) return mergedSourceSkip;
    logProgress("replacement branch has no changes versus base; skipping PR create", {
      branch,
      base_branch: baseBranch,
      commit: prep.commit,
    });
    return {
      action: "open_fix_pr",
      status: "skipped",
      branch,
      resumed_branch: branchState.resumed,
      commit: prep.commit,
      checkpoint_commits: prep.checkpoint_commits,
      merge_preflight: prep.merge_preflight,
      supersede_sources: [],
      contributor_credit: contributorCredits.map(publicContributorCredit),
      reason: "replacement branch has no changes versus base after repair",
    };
  }

  pushRecoverableBranch({ targetDir, branch });
  const bodyPath = path.join(workRoot, "replacement-pr-body.md");
  fs.writeFileSync(bodyPath, body);
  const prUrl =
    findOpenPullRequestForBranch(branch, targetDir) ||
    run(
      "gh",
      [
        "pr",
        "create",
        "--repo",
        result.repo,
        "--base",
        baseBranch,
        "--head",
        branch,
        "--title",
        fixArtifact.pr_title,
        "--body-file",
        bodyPath,
      ],
      { cwd: targetDir, env: ghEnv(), timeoutMs: currentNetworkCommandTimeoutMs() },
    ).trim();
  const prNumber = pullRequestNumberFromUrl(prUrl);
  if (prNumber) ensurePullRequestOpen({ number: prNumber, targetDir });
  if (prNumber) labelReplacementPullRequest({ number: prNumber, targetDir, fixArtifact });
  if (prNumber) (prep.merge_preflight as JsonValue).target = `#${prNumber}`;
  const threadResolution = prNumber
    ? prepareReviewThreadsForMerge({
        repo: result.repo,
        number: prNumber,
        targetDir,
        resolveThreads: resolveReviewThreads,
      })
    : { status: "blocked", reason: "replacement PR URL did not include a PR number" };

  const supersededSources = supersedeSources
    ? supersededReplacementSources({ fixArtifact, repo: result.repo }).filter(
        (source: JsonValue) => pullRequestNumberFromUrl(source) !== prNumber,
      )
    : [];
  const supersededSourceActions: JsonValue[] = [];
  if (supersededSources.length > 0) {
    for (const source of supersededSources) {
      const parsed = parsePullRequestUrl(source);
      if (!parsed || parsed.repo !== result.repo) continue;
      supersededSourceActions.push(
        closeSupersededSourcePrs
          ? closeSupersededSourcePr({
              source,
              parsed,
              replacementPrUrl: prUrl,
              targetDir,
              contributorCredits,
              provenance,
            })
          : linkReplacementSourcePr({
              source,
              parsed,
              replacementPrUrl: prUrl,
              targetDir,
              provenance,
            }),
      );
    }
  }

  return {
    action: "open_fix_pr",
    status: "opened",
    pr_url: prUrl,
    branch,
    resumed_branch: branchState.resumed,
    commit: prep.commit,
    checkpoint_commits: prep.checkpoint_commits,
    merge_preflight: prep.merge_preflight,
    review_threads: threadResolution,
    superseded_sources: supersededSources,
    superseded_source_actions: supersededSourceActions,
    contributor_credit: contributorCredits.map(publicContributorCredit),
  };
}

function mergedReplacementSourcePr({ fixArtifact, sourcePr = null, targetDir }: LooseRecord) {
  const sources = [...(sourcePr?.url ? [sourcePr.url] : []), ...(fixArtifact.source_prs ?? [])];
  for (const source of uniqueStrings(sources)) {
    const parsed = parsePullRequestUrl(source);
    if (!parsed || parsed.repo !== result.repo) continue;
    const view = fetchSourcePullRequestView({
      repo: result.repo,
      number: parsed.number,
      targetDir,
    });
    if (view.mergedAt || view.state === "MERGED") {
      return {
        source,
        pr: `#${parsed.number}`,
        merged_at: view.mergedAt ?? null,
      };
    }
  }
  return null;
}

function skipMergedSourceReplacementWithoutDiff({
  mergedSource,
  targetDir,
  baseBranch,
  branch,
  fallbackReason = null,
  sourcePr = null,
  prep,
  contributorCredits,
  resumedBranch = null,
}: LooseRecord) {
  if (!mergedSource) return null;
  if (branchHasBaseDiff({ targetDir, baseBranch })) return null;
  logProgress(
    "source PR already merged and replacement branch has no changes; skipping PR create",
    {
      branch,
      base_branch: baseBranch,
      source_pr: mergedSource.source,
      merged_at: mergedSource.merged_at ?? null,
    },
  );
  return {
    action: "open_fix_pr",
    status: "skipped",
    branch,
    ...(resumedBranch !== null ? { resumed_branch: resumedBranch } : {}),
    repair_strategy: "replace_uneditable_branch",
    ...(sourcePr ? { fallback_from: "repair_contributor_branch" } : {}),
    ...(sourcePr?.url ? { fallback_source_pr: sourcePr.url } : {}),
    ...(fallbackReason ? { fallback_reason: fallbackReason } : {}),
    commit: prep.commit,
    checkpoint_commits: prep.checkpoint_commits,
    merge_preflight: prep.merge_preflight,
    supersede_sources: [],
    merged_source_pr: mergedSource.pr,
    merged_source_url: mergedSource.source,
    merged_source_at: mergedSource.merged_at ?? null,
    contributor_credit: contributorCredits.map(publicContributorCredit),
    reason: "source PR already merged and replacement branch has no changes versus base",
  };
}

function labelReplacementPullRequest({ number, targetDir, fixArtifact }: LooseRecord) {
  ensureLabel(
    result.repo,
    CLAWSWEEPER_LABEL,
    CLAWSWEEPER_LABEL_COLOR,
    CLAWSWEEPER_LABEL_DESCRIPTION,
    targetDir,
  );
  for (const label of replacementSourceLabels({ fixArtifact, targetDir })) {
    addLabel(result.repo, number, label, targetDir);
  }
  if (job.frontmatter.source === "issue_implementation") {
    ensureLabel(
      result.repo,
      AUTOGENERATED_LABEL,
      AUTOGENERATED_LABEL_COLOR,
      AUTOGENERATED_LABEL_DESCRIPTION,
      targetDir,
    );
    addLabel(result.repo, number, AUTOGENERATED_LABEL, targetDir);
  }
  if (job.frontmatter.source === "clawsweeper_commit" || job.frontmatter.commit_sha) {
    ensureLabel(
      result.repo,
      COMMIT_FINDING_LABEL,
      COMMIT_FINDING_LABEL_COLOR,
      COMMIT_FINDING_LABEL_DESCRIPTION,
      targetDir,
    );
    addLabel(result.repo, number, COMMIT_FINDING_LABEL, targetDir);
  }
}

function replacementSourceLabels({ fixArtifact, targetDir }: LooseRecord) {
  const sourceLabelSets = replacementSourceLabelSets({ fixArtifact, targetDir });
  return replacementLabelsToCopy(sourceLabelSets, [CLAWSWEEPER_LABEL]);
}

function replacementSourceLabelSets({ fixArtifact, targetDir }: LooseRecord) {
  const sourceLabelSets: string[][] = [];
  for (const source of fixArtifact.source_prs ?? []) {
    const parsed = parsePullRequestUrl(source);
    if (!parsed || parsed.repo !== result.repo) continue;
    sourceLabelSets.push(sourcePullRequestLabels({ number: parsed.number, targetDir }));
  }
  return sourceLabelSets;
}

function sourcePullRequestLabels({ number, targetDir }: LooseRecord) {
  const view = JSON.parse(
    run("gh", ["pr", "view", String(number), "--repo", result.repo, "--json", "labels"], {
      cwd: targetDir,
      env: ghEnv(),
      timeoutMs: currentNetworkCommandTimeoutMs(),
    }),
  );
  return (view.labels ?? [])
    .map((label: JsonValue) => String(label?.name ?? label ?? "").trim())
    .filter(Boolean);
}

function ensureLabel(
  repo: string,
  name: string,
  color: JsonValue,
  description: JsonValue,
  targetDir: string,
) {
  try {
    run(
      "gh",
      ["label", "create", name, "--repo", repo, "--color", color, "--description", description],
      {
        cwd: targetDir,
        env: ghEnv(),
        timeoutMs: currentNetworkCommandTimeoutMs(),
      },
    );
  } catch (error) {
    if (!/already exists/i.test(String(error?.message ?? error))) throw error;
  }
}

function addLabel(repo: string, number: JsonValue, name: string, targetDir: string) {
  run("gh", ["issue", "edit", String(number), "--repo", repo, "--add-label", name], {
    cwd: targetDir,
    env: ghEnv(),
    timeoutMs: currentNetworkCommandTimeoutMs(),
  });
}

function linkReplacementSourcePr({
  source,
  parsed,
  replacementPrUrl,
  targetDir,
  contributorCredits,
  provenance,
}: LooseRecord) {
  const base = { source, pr: `#${parsed.number}`, action: "link_replacement_source" };
  const view = fetchSourcePullRequestView({ repo: result.repo, number: parsed.number, targetDir });
  if (view.mergedAt || view.state === "MERGED") {
    return {
      ...base,
      status: "skipped",
      reason: "already merged",
      merged_at: view.mergedAt ?? null,
    };
  }
  if (view.state === "CLOSED") {
    return { ...base, status: "skipped", reason: "already closed" };
  }

  const comment = replacementSourceLinkComment({
    replacementPrUrl,
    sourcePrUrl: source,
    provenance,
    contributorCredits,
  });
  run("gh", ["pr", "comment", String(parsed.number), "--repo", result.repo, "--body", comment], {
    cwd: targetDir,
    env: ghEnv(),
    timeoutMs: currentNetworkCommandTimeoutMs(),
  });
  return { ...base, status: "executed", reason: "linked replacement PR without closing source PR" };
}

function closeSupersededSourcePr({
  source,
  parsed,
  replacementPrUrl,
  targetDir,
  contributorCredits,
  provenance,
}: LooseRecord) {
  const base = { source, pr: `#${parsed.number}`, action: "close_superseded_source" };
  const view = fetchSourcePullRequestView({ repo: result.repo, number: parsed.number, targetDir });
  if (view.mergedAt || view.state === "MERGED") {
    return {
      ...base,
      status: "skipped",
      reason: "already merged",
      merged_at: view.mergedAt ?? null,
    };
  }
  if (view.state === "CLOSED") {
    return { ...base, status: "skipped", reason: "already closed" };
  }

  const comment = replacementSourceCloseComment({
    replacementPrUrl,
    sourcePrUrl: source,
    provenance,
    contributorCredits,
  });
  run("gh", ["pr", "comment", String(parsed.number), "--repo", result.repo, "--body", comment], {
    cwd: targetDir,
    env: ghEnv(),
    timeoutMs: currentNetworkCommandTimeoutMs(),
  });

  try {
    run("gh", ["pr", "close", String(parsed.number), "--repo", result.repo], {
      cwd: targetDir,
      env: ghEnv(),
      timeoutMs: currentNetworkCommandTimeoutMs(),
    });
    return {
      ...base,
      status: "executed",
      reason: "closed in favor of credited replacement PR",
      replacement_pr: replacementPrUrl,
      contributor_credit: contributorCredits.map(publicContributorCredit),
    };
  } catch (error) {
    const detail = error instanceof Error ? error.message : String(error);
    if (/already merged|can't be closed because it was already merged/i.test(detail)) {
      return { ...base, status: "skipped", reason: "already merged during close" };
    }
    throw error;
  }
}

function ensurePullRequestOpen({ number, targetDir }: LooseRecord) {
  const view = fetchSourcePullRequestView({ repo: result.repo, number, targetDir });
  if (view.mergedAt || view.state === "MERGED") {
    throw new Error(`replacement PR #${number} is already merged`);
  }
  if (view.state === "CLOSED") {
    run("gh", ["pr", "reopen", String(number), "--repo", result.repo], {
      cwd: targetDir,
      env: ghEnv(),
      timeoutMs: currentNetworkCommandTimeoutMs(),
    });
  }
}

function editValidatePrepareMerge({
  fixArtifact,
  targetDir,
  branch,
  mode,
  fallbackReason,
  baseBranch = DEFAULT_BASE_BRANCH,
  contributorCredits = [],
  allowExistingChanges = false,
  reconcileWithBase = false,
  pushCheckpoint = null,
  sourceHead = null,
  rebaseResult = null,
}: LooseRecord) {
  let producedChanges = allowExistingChanges;
  let previousSummary = "";
  const checkpointCommits: JsonValue[] = [];
  if (
    !producedChanges &&
    canTreatRebaseAsCompleteRepair({
      fixArtifact,
      rebaseResult,
    })
  ) {
    producedChanges = true;
    logProgress("deterministic rebase changed branch head; skipping Codex edit pass", {
      previous_head: rebaseResult.previous_head,
      current_head: rebaseResult.current_head,
    });
  }
  const rebaseUnmergedPaths = !producedChanges ? unmergedPaths(targetDir) : [];
  if (
    !producedChanges &&
    canTreatRebaseAsCompleteRepair({
      fixArtifact,
      rebaseResult,
      hasUnmergedPaths: rebaseUnmergedPaths.length > 0,
    })
  ) {
    const completed = completeMechanicallyResolvedRebase({ targetDir });
    if (completed.status === "continued") {
      producedChanges = true;
      logProgress("completed mechanically resolved rebase", {
        previous_head: completed.previous_head,
        current_head: completed.current_head,
      });
    }
  }
  if (!producedChanges && !reconcileWithBase) {
    const mechanicalFix = applyMechanicalChangelogFix({
      fixArtifact,
      repo: result.repo,
      targetDir,
    });
    producedChanges = mechanicalFix?.status === "applied";
    if (producedChanges) logProgress("applied mechanical changelog fix");
  }
  const repositoryContext = buildRepositoryContext({ fixArtifact, targetDir });
  const shouldRunCodexEdit = !producedChanges || reconcileWithBase;
  const repairDeltaBaseHead =
    rebaseResult?.status === "conflicts" ? sourceHead : currentHead(targetDir);
  if (shouldRunCodexEdit) {
    for (let attempt = 1; attempt <= maxEditAttempts; attempt += 1) {
      const headBeforeAttempt = currentHead(targetDir);
      const prompt = buildFixPrompt({
        fixArtifact,
        branch,
        mode,
        fallbackReason,
        attempt,
        previousNoDiff: attempt > 1,
        previousSummary,
        repositoryContext,
        reconcileWithBase,
        sourceHead,
        rebaseResult,
        maxEditAttempts,
        validationCommands: validationPreflight.resolved_commands ?? [],
        isAutomergeRepair: isAutomergeRepairJob(),
      });
      const summaryPath = path.join(workRoot, `${mode}-codex-summary-${attempt}.md`);
      const workerTimeoutMs = currentCodexTimeoutMs();
      logProgress("starting Codex edit pass", {
        mode,
        attempt,
        timeout_ms: workerTimeoutMs,
        reconcile_with_base: reconcileWithBase,
        rebase_status: rebaseResult?.status ?? null,
      });
      updateAutomergeProgressStatus({
        id: `codex-edit-${mode}-${attempt}`,
        label: `Codex edit ${attempt}`,
        status: "running",
        details: reconcileWithBase ? "reconciling latest base" : "repairing branch",
        headSha: headBeforeAttempt,
      });
      const codexResult = spawnCodexSyncWithHeartbeat(
        `Codex fix worker ${mode} attempt ${attempt}`,
        [
          "exec",
          "--cd",
          targetDir,
          "--model",
          model,
          "--sandbox",
          codexWriteSandbox,
          ...codexWriteSandboxConfigArgs(),
          ...codexConfigArgs(),
          "--output-last-message",
          summaryPath,
          "--ephemeral",
          "--json",
          "-",
        ],
        {
          cwd: targetDir,
          input: prompt,
          encoding: "utf8",
          env: codexEnv(),
          timeout: workerTimeoutMs,
          maxBuffer: codexStdioMaxBuffer,
        },
      );
      fs.writeFileSync(
        path.join(workRoot, `${mode}-codex-${attempt}.jsonl`),
        codexResult.stdout ?? "",
      );
      if (codexResult.stderr)
        fs.writeFileSync(
          path.join(workRoot, `${mode}-codex-${attempt}.stderr.log`),
          codexResult.stderr,
        );
      if ((codexResult.error as JsonValue)?.code === "ETIMEDOUT") {
        throw new Error(`Codex fix worker timed out after ${workerTimeoutMs}ms`);
      }
      if (codexResult.error) {
        const errorDetail = codexFailureDetail(
          codexResult,
          codexResult.error.message || String(codexResult.error),
        );
        if (attempt < maxEditAttempts && isRetryableCodexTransportError(errorDetail)) {
          previousSummary = compactText(errorDetail, 360);
          const retryDelayMs = codexRetryDelayMs(errorDetail, attempt);
          logProgress("retrying Codex edit pass after transient transport error", {
            mode,
            attempt,
            max_attempts: maxEditAttempts,
            retry_delay_ms: retryDelayMs,
          });
          updateAutomergeProgressStatus({
            id: `codex-edit-${mode}-${attempt}`,
            label: `Codex edit ${attempt}`,
            status: "retrying",
            details: "transient Codex transport error",
            headSha: currentHead(targetDir),
          });
          sleepMs(retryDelayMs);
          continue;
        }
        throw new Error(codexFailureMessage("Codex fix worker failed", errorDetail));
      }
      if (codexResult.status !== 0) {
        const errorDetail = codexFailureDetail(codexResult, "Codex fix worker failed");
        if (attempt < maxEditAttempts && isRetryableCodexTransportError(errorDetail)) {
          previousSummary = compactText(errorDetail, 360);
          const retryDelayMs = codexRetryDelayMs(errorDetail, attempt);
          logProgress("retrying Codex edit pass after transient transport error", {
            mode,
            attempt,
            max_attempts: maxEditAttempts,
            retry_delay_ms: retryDelayMs,
          });
          updateAutomergeProgressStatus({
            id: `codex-edit-${mode}-${attempt}`,
            label: `Codex edit ${attempt}`,
            status: "retrying",
            details: "transient Codex transport error",
            headSha: currentHead(targetDir),
          });
          sleepMs(retryDelayMs);
          continue;
        }
        throw new Error(codexFailureMessage("Codex fix worker failed", errorDetail));
      }
      logProgress("Codex edit pass finished", { mode, attempt, status: codexResult.status });
      updateAutomergeProgressStatus({
        id: `codex-edit-${mode}-${attempt}`,
        label: `Codex edit ${attempt}`,
        status: "complete",
        details: `exit ${codexResult.status}`,
        headSha: currentHead(targetDir),
      });

      const hasWorkingTreeChanges = Boolean(
        run("git", ["status", "--porcelain"], { cwd: targetDir }).trim(),
      );
      const hasHeadChanges = currentHead(targetDir) !== headBeforeAttempt;
      producedChanges = producedChanges || hasWorkingTreeChanges || hasHeadChanges;
      if (producedChanges) break;
      previousSummary = readTextIfExists(summaryPath).trim();
    }
  }
  if (!producedChanges) {
    const suffix = previousSummary
      ? ` Last Codex summary: ${compactText(previousSummary, 360)}`
      : "";
    throw new Error(
      `Codex produced no target repo changes after ${maxEditAttempts} edit attempt(s).${suffix}`,
    );
  }

  const completedRebase = completeRebaseIfResolved({ targetDir });
  if (completedRebase.status === "continued") {
    producedChanges = true;
    logProgress("completed resolved rebase", {
      previous_head: completedRebase.previous_head,
      current_head: completedRebase.current_head,
    });
  }

  const firstCheckpoint = commitCheckpointIfNeeded({
    targetDir,
    message: fixArtifact.pr_title,
    trailers: mode === "replacement" ? coAuthorTrailers(contributorCredits) : [],
  });
  if (firstCheckpoint) {
    checkpointCommits.push(firstCheckpoint);
    pushCheckpoint?.();
  }

  let codexReview = null;
  const maxFinalBaseSyncAttempts = Math.max(
    1,
    Number(process.env.CLAWSWEEPER_FINAL_BASE_SYNC_ATTEMPTS ?? 1),
  );
  for (let attempt = 1; attempt <= maxFinalBaseSyncAttempts; attempt += 1) {
    logProgress("starting validation/review loop", { mode, attempt });
    updateAutomergeProgressStatus({
      id: `validation-review-${mode}-${attempt}`,
      label: `validation and review ${attempt}`,
      status: "running",
      details: mode,
      headSha: currentHead(targetDir),
    });
    codexReview = validateAndReviewLoop({
      fixArtifact,
      targetDir,
      mode,
      baseBranch,
      sourceHead: repairDeltaBaseHead,
      onReviewFix: (reviewAttempt: JsonValue) => {
        const checkpoint = commitCheckpointIfNeeded({
          targetDir,
          message: `fix(clawsweeper): address review for ${result.cluster_id} (${reviewAttempt})`,
          trailers: mode === "replacement" ? coAuthorTrailers(contributorCredits) : [],
        });
        if (checkpoint) {
          checkpointCommits.push(checkpoint);
          pushCheckpoint?.();
        }
      },
    });
    const sync = reconcileLatestBaseBeforePush({
      fixArtifact,
      targetDir,
      branch,
      mode,
      baseBranch,
      contributorCredits,
      attempt,
      repositoryContext,
      sourceHead,
    });
    logProgress("final base sync result", { mode, attempt, status: sync.status });
    updateAutomergeProgressStatus({
      id: `validation-review-${mode}-${attempt}`,
      label: `validation and review ${attempt}`,
      status: sync.status === "already-current" ? "complete" : "base moved",
      details: sync.status,
      headSha: currentHead(targetDir),
    });
    if (sync.status === "already-current") break;
    const checkpoint = commitCheckpointIfNeeded({
      targetDir,
      message: `fix(clawsweeper): reconcile ${result.cluster_id} with main (${attempt})`,
      trailers: mode === "replacement" ? coAuthorTrailers(contributorCredits) : [],
    });
    if (checkpoint) {
      checkpointCommits.push(checkpoint);
      pushCheckpoint?.();
    }
    if (attempt === maxFinalBaseSyncAttempts) {
      codexReview.final_base_sync = {
        status: "accepted_after_final_sync",
        reason:
          "origin/main moved during final validation; pushed the branch after the last successful final-base sync and left review/CI/automerge to gate the exact head",
        attempts: maxFinalBaseSyncAttempts,
        sync,
      };
      break;
    }
  }
  const finalCheckpoint = commitCheckpointIfNeeded({
    targetDir,
    message: `fix(clawsweeper): finalize ${result.cluster_id}`,
    trailers: mode === "replacement" ? coAuthorTrailers(contributorCredits) : [],
  });
  if (finalCheckpoint) {
    checkpointCommits.push(finalCheckpoint);
    pushCheckpoint?.();
  }
  const commit = run("git", ["rev-parse", "HEAD"], { cwd: targetDir }).trim();
  return {
    commit,
    checkpoint_commits: checkpointCommits,
    merge_preflight: buildMergePreflight({ fixArtifact, codexReview }),
  };
}

function logProgress(message: string, details: LooseRecord = {}) {
  const suffix = Object.keys(details).length > 0 ? ` ${JSON.stringify(details)}` : "";
  console.log(`[clawsweeper repair] ${new Date().toISOString()} ${message}${suffix}`);
}

function updateAutomergeProgressStatus({
  id,
  label,
  status,
  details = null,
  headSha = null,
}: LooseRecord) {
  if (!isAutomergeRepairJob() || dryRun) return false;
  const target = automergeOutcomeTargetPrNumber();
  if (!target) return false;
  try {
    const existingStatus = findAutomergeStatusComment(target);
    if (!existingStatus?.id) return false;
    const now = new Date();
    const bodyWithTimeline = mergeAutomergeTimelineSection({
      body: existingStatus.body,
      existingBody: existingStatus.body,
      events: [
        {
          id: `repair-progress:${currentActionsRunId() || path.basename(path.dirname(resultPath))}:${id}`,
          label,
          at: scriptStartedAt.toISOString(),
          completedAt: now.toISOString(),
          durationMs: now.getTime() - scriptStartedAt.getTime(),
          runUrl: currentActionsRunUrl(),
          headSha: headSha ?? automergeOutcomeReviewedSha(),
          repo: result.repo,
          status,
          details,
        },
      ],
    });
    patchIssueComment(
      existingStatus.id,
      preserveStatusMarkers(existingStatus.body, bodyWithTimeline),
    );
    return true;
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error);
    console.warn(`[clawsweeper repair] failed to update automerge progress status: ${message}`);
    return false;
  }
}

function reconcileLatestBaseBeforePush({
  fixArtifact,
  targetDir,
  branch,
  mode,
  baseBranch,
  attempt,
  repositoryContext,
  sourceHead,
}: LooseRecord) {
  runGitNetwork(["fetch", "origin", `${baseBranch}:refs/remotes/origin/${baseBranch}`], targetDir);
  const baseRef = `origin/${baseBranch}`;
  if (isAncestor({ targetDir, ancestor: baseRef, descendant: "HEAD" })) {
    return { status: "already-current" };
  }

  const rebaseResult = rebaseOntoBase({ targetDir, baseBranch });
  if (rebaseResult.status !== "conflicts") return rebaseResult;

  runCodexBaseReconcile({
    fixArtifact,
    targetDir,
    branch,
    mode,
    baseBranch,
    attempt,
    repositoryContext,
    sourceHead,
    rebaseResult,
  });
  const completed = completeRebaseIfResolved({ targetDir });
  return {
    status: "codex-reconciled",
    rebase_result: rebaseResult,
    completed_rebase: completed,
  };
}

function runCodexBaseReconcile({
  fixArtifact,
  targetDir,
  branch,
  mode,
  attempt,
  repositoryContext,
  sourceHead,
  rebaseResult,
}: LooseRecord) {
  let previousSummary = "";
  for (let codexAttempt = 1; codexAttempt <= maxEditAttempts; codexAttempt += 1) {
    const prompt = buildFixPrompt({
      fixArtifact,
      branch,
      mode,
      fallbackReason:
        "origin/main advanced after validation. Resolve this final rebase so the branch is mergeable on current main, then leave the checkout in a normal non-rebasing state.",
      attempt: codexAttempt,
      previousNoDiff: codexAttempt > 1,
      previousSummary,
      repositoryContext,
      reconcileWithBase: true,
      sourceHead,
      rebaseResult,
      maxEditAttempts,
      validationCommands: validationPreflight.resolved_commands ?? [],
      isAutomergeRepair: isAutomergeRepairJob(),
    });
    const summaryPath = path.join(
      workRoot,
      `${mode}-final-base-reconcile-summary-${attempt}-${codexAttempt}.md`,
    );
    const reconcileTimeoutMs = currentCodexTimeoutMs();
    const codexResult = spawnCodexSyncWithHeartbeat(
      `Codex final rebase worker ${mode} attempt ${attempt}.${codexAttempt}`,
      [
        "exec",
        "--cd",
        targetDir,
        "--model",
        model,
        "--sandbox",
        codexWriteSandbox,
        ...codexWriteSandboxConfigArgs(),
        ...codexConfigArgs(),
        "--output-last-message",
        summaryPath,
        "--ephemeral",
        "--json",
        "-",
      ],
      {
        cwd: targetDir,
        input: prompt,
        encoding: "utf8",
        env: codexEnv(),
        timeout: reconcileTimeoutMs,
        maxBuffer: codexStdioMaxBuffer,
      },
    );
    fs.writeFileSync(
      path.join(workRoot, `${mode}-final-base-reconcile-${attempt}-${codexAttempt}.jsonl`),
      codexResult.stdout ?? "",
    );
    if (codexResult.stderr)
      fs.writeFileSync(
        path.join(workRoot, `${mode}-final-base-reconcile-${attempt}-${codexAttempt}.stderr.log`),
        codexResult.stderr,
      );
    if ((codexResult.error as JsonValue)?.code === "ETIMEDOUT") {
      throw new Error(`Codex final rebase worker timed out after ${reconcileTimeoutMs}ms`);
    }
    if (codexResult.error) {
      throw new Error(codexResult.error.message || String(codexResult.error));
    }
    if (codexResult.status !== 0) {
      throw new Error(
        codexResult.stderr || codexResult.stdout || "Codex final rebase worker failed",
      );
    }
    try {
      completeRebaseIfResolved({ targetDir });
      return;
    } catch (error) {
      if (codexAttempt === maxEditAttempts) throw error;
      previousSummary = readTextIfExists(summaryPath).trim();
    }
  }
  throw new Error(`Codex did not finish final rebase after ${maxEditAttempts} attempt(s)`);
}

function readTextIfExists(filePath: string) {
  return fs.existsSync(filePath) ? fs.readFileSync(filePath, "utf8") : "";
}

