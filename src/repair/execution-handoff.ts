function publishReplacementRepair({
  checkout,
  intent,
  publication,
  publicationCheckpoint,
  mutations,
  checkpointedClosures,
  expectedMutationActor,
}: {
  checkout: string;
  intent: ExecutionIntent;
  publication: PreparedPublication;
  publicationCheckpoint: PublicationReceipt | null;
  mutations: LooseRecord[];
  checkpointedClosures: ReadonlySet<string>;
  expectedMutationActor: string;
}): number {
  const pulls = pullsForBranch(
    intent.target_repo,
    intent.output_branch,
    intent.target_base_ref,
    "all",
  );
  const recovery = selectAuthorizedReplacementPull({
    pulls,
    publication,
    intent,
    publicationCheckpoint,
    trustedCloseEvidenceByNumber: trustedPullCloseEvidenceByNumber(
      intent.target_repo,
      pulls,
      expectedMutationActor,
    ),
  });
  const liveTargetPr = recovery?.number ?? null;
  if (
    intent.expected_target_pr_number !== null &&
    liveTargetPr !== intent.expected_target_pr_number
  ) {
    throw new Error("target pull request changed after authorization");
  }
  const targetRef = `refs/heads/${intent.output_branch}`;
  publishPreparedRef({
    checkout,
    intent,
    publication,
    targetRef,
    expectedSha: intent.expected_output_sha,
    targetPrNumber: liveTargetPr,
    checkpointedClosures,
  });
  mutations.push({
    operation: "push",
    repo: intent.target_repo,
    ref: targetRef,
    previous_sha: intent.expected_output_sha,
    sha: publication.prepared_head_sha,
  });

  let targetPrNumber = intent.expected_target_pr_number;
  let createdTargetPr = false;
  if (!targetPrNumber && liveTargetPr) {
    if (recovery?.state === "reopen") {
      runPublicationMutation({
        intent,
        publication,
        checkpointedClosures,
        targetNumbers: [liveTargetPr],
        mutation: () =>
          runExactReplacementReopenMutation({
            readPull: () => ghObject(`repos/${intent.target_repo}/pulls/${liveTargetPr}`),
            expectedNumber: liveTargetPr,
            publication,
            intent,
            publicationCheckpoint,
            readStateEvents: () => readSourcePullStateEvents(intent.target_repo, liveTargetPr),
            expectedCloseActor: expectedMutationActor,
            mutation: () =>
              run("gh", ["pr", "reopen", String(liveTargetPr), "--repo", intent.target_repo], {
                cwd: checkout,
              }),
          }),
      });
      mutations.push({
        operation: "reopen_pull_request",
        repo: intent.target_repo,
        pull_number: liveTargetPr,
      });
    }
    verifyPublishedPull(intent.target_repo, liveTargetPr, publication, intent, false);
    targetPrNumber = liveTargetPr;
  } else if (targetPrNumber) {
    const expectedTargetPrNumber = targetPrNumber;
    if (recovery?.state === "reopen") {
      runPublicationMutation({
        intent,
        publication,
        checkpointedClosures,
        targetNumbers: [expectedTargetPrNumber],
        mutation: () =>
          runExactReplacementReopenMutation({
            readPull: () => ghObject(`repos/${intent.target_repo}/pulls/${expectedTargetPrNumber}`),
            expectedNumber: expectedTargetPrNumber,
            publication,
            intent,
            publicationCheckpoint,
            readStateEvents: () =>
              readSourcePullStateEvents(intent.target_repo, expectedTargetPrNumber),
            expectedCloseActor: expectedMutationActor,
            mutation: () =>
              run(
                "gh",
                ["pr", "reopen", String(expectedTargetPrNumber), "--repo", intent.target_repo],
                {
                  cwd: checkout,
                },
              ),
          }),
      });
      mutations.push({
        operation: "reopen_pull_request",
        repo: intent.target_repo,
        pull_number: expectedTargetPrNumber,
      });
    }
    verifyPublishedPull(intent.target_repo, expectedTargetPrNumber, publication, intent, false);
  } else {
    const bodyPath = path.join(path.dirname(checkout), "pull-request-body.md");
    fs.writeFileSync(bodyPath, publication.pr_body ?? "");
    const created = runPublicationMutation({
      intent,
      publication,
      checkpointedClosures,
      targetNumbers: [],
      mutation: () =>
        run(
          "gh",
          [
            "pr",
            "create",
            "--repo",
            intent.target_repo,
            "--base",
            intent.target_base_ref,
            "--head",
            intent.output_branch,
            "--title",
            publication.pr_title ?? "",
            "--body-file",
            bodyPath,
          ],
          { cwd: checkout },
        ),
    }).trim();
    const parsed = parsePullRequestUrl(created);
    if (!parsed || parsed.repo !== intent.target_repo) {
      throw new Error("created pull request URL escaped the authorized target repository");
    }
    targetPrNumber = parsed.number;
    createdTargetPr = true;
    verifyPublishedPull(intent.target_repo, targetPrNumber, publication, intent, false);
  }
  if (targetPrNumber === null) {
    throw new Error("authorized output pull request was not created");
  }
  if (createdTargetPr) {
    mutations.push({
      operation: "open_pull_request",
      repo: intent.target_repo,
      pull_number: targetPrNumber,
      branch: intent.output_branch,
    });
  }
  publishRequiredPullLabels({
    checkout,
    intent,
    publication,
    targetPrNumber,
    checkpointedClosures,
  });
  mutations.push({
    operation: "label_pull_request",
    repo: intent.target_repo,
    pull_number: targetPrNumber,
    labels: intent.required_labels,
  });
  verifyPublishedPull(intent.target_repo, targetPrNumber, publication, intent);
  assertPublicationSafe(intent, [targetPrNumber]);
  return targetPrNumber;
}

export function selectAuthorizedReplacementPull({
  pulls,
  publication,
  intent,
  publicationCheckpoint = null,
  trustedCloseEvidenceByNumber = new Map(),
}: {
  pulls: LooseRecord[];
  publication: PreparedPublication;
  intent: ExecutionIntent;
  publicationCheckpoint?: PublicationReceipt | null;
  trustedCloseEvidenceByNumber?: ReadonlyMap<number, SourceCloseEvidence>;
}): { number: number; state: "open" | "reopen" } | null {
  const allowedHeadShas = new Set(
    [intent.expected_output_sha, publication.prepared_head_sha].filter((sha): sha is string =>
      Boolean(sha),
    ),
  );
  const exact = pulls.filter(
    (pull) =>
      pull.head?.repo?.full_name === intent.output_repo &&
      pull.head?.ref === intent.output_branch &&
      allowedHeadShas.has(String(pull.head?.sha ?? "")) &&
      pull.base?.ref === intent.target_base_ref &&
      pull.title === publication.pr_title &&
      pull.body === publication.pr_body,
  );
  if (exact.length > 1) {
    throw new Error("authorized output branch has multiple exact pull request targets");
  }
  if (exact.length === 0) {
    if (pulls.some((pull) => String(pull.state ?? "").toLowerCase() === "open")) {
      throw new Error("existing output pull request does not match the authorized publication");
    }
    return null;
  }

  const pull = exact[0]!;
  const number = Number(pull.number);
  if (!Number.isInteger(number) || number <= 0) {
    throw new Error("authorized output pull request number is invalid");
  }
  const state = String(pull.state ?? "").toLowerCase();
  if (pull.merged_at || pull.mergedAt) {
    throw new Error("authorized replacement pull request is already merged");
  }
  if (state === "open") return { number, state: "open" };
  if (state === "closed") {
    if (!publicationCheckpoint) {
      throw new Error("closed replacement pull request lacks a verified publication checkpoint");
    }
    verifyPublicationReceipt({
      receipt: publicationCheckpoint,
      publication,
      validationReceiptSha256: publicationCheckpoint.validation_receipt_sha256,
    });
    if (
      publicationCheckpoint.operation !== "open_pull_request" ||
      publicationCheckpoint.target_pr_number !== number
    ) {
      throw new Error("publication checkpoint does not authorize reopening this replacement");
    }
    if (!trustedCloseEvidenceByNumber.has(number)) {
      throw new Error("closed replacement pull request lacks trusted ClawSweeper close provenance");
    }
    return { number, state: "reopen" };
  }
  throw new Error(`authorized replacement pull request is ${state || "unknown"}`);
}

export function runExactReplacementReopenMutation<T>({
  readPull,
  expectedNumber,
  publication,
  intent,
  publicationCheckpoint,
  readStateEvents,
  expectedCloseActor,
  mutation,
}: {
  readPull: () => LooseRecord;
  expectedNumber: number;
  publication: PreparedPublication;
  intent: ExecutionIntent;
  publicationCheckpoint: PublicationReceipt | null;
  readStateEvents: () => readonly SourcePullStateEvent[];
  expectedCloseActor: string;
  mutation: () => T;
}): T {
  const pull = readPull();
  const closeEvidence = trustedPullCloseEvidence({
    events: readStateEvents(),
    expectedCloseActor,
  });
  const selection = selectAuthorizedReplacementPull({
    pulls: [pull],
    publication,
    intent,
    publicationCheckpoint,
    trustedCloseEvidenceByNumber: closeEvidence
      ? new Map([[expectedNumber, closeEvidence]])
      : new Map(),
  });
  if (!selection || selection.number !== expectedNumber || selection.state !== "reopen") {
    throw new Error("replacement pull request is no longer the exact closed authorized target");
  }
  return mutation();
}

export function trustedPullCloseEvidence({
  events,
  expectedCloseActor,
}: {
  events: readonly SourcePullStateEvent[];
  expectedCloseActor: string;
}): SourceCloseEvidence | null {
  const expectedActor = requiredCloseActor(expectedCloseActor);
  const ordered = [...events].sort((left, right) => left.id - right.id);
  if (new Set(ordered.map((event) => event.id)).size !== ordered.length) {
    throw new Error("pull request state events contain duplicate ids");
  }
  const latest = ordered.at(-1);
  if (
    !latest ||
    latest.event !== "closed" ||
    normalizeActor(latest.actor) !== normalizeActor(expectedActor) ||
    !validEventTimestamp(latest.created_at)
  ) {
    return null;
  }
  return {
    close_event_id: requiredEventId(latest.id, "pull request close event id"),
    close_actor: latest.actor,
    closed_at: latest.created_at,
  };
}

function trustedPullCloseEvidenceByNumber(
  repo: string,
  pulls: readonly LooseRecord[],
  expectedCloseActor: string,
): Map<number, SourceCloseEvidence> {
  const evidence = new Map<number, SourceCloseEvidence>();
  for (const pull of pulls) {
    if (String(pull.state ?? "").toLowerCase() !== "closed") continue;
    const number = Number(pull.number);
    if (!Number.isSafeInteger(number) || number <= 0) continue;
    const trusted = trustedPullCloseEvidence({
      events: readSourcePullStateEvents(repo, number),
      expectedCloseActor,
    });
    if (trusted) evidence.set(number, trusted);
  }
  return evidence;
}

export function sourceCloseoutStateBlock({
  source,
  state,
  completed,
  pending = false,
}: {
  source: string;
  state: string;
  completed: boolean;
  pending?: boolean;
}): string {
  if (completed) {
    if (state === "closed") return "";
    if (state === "open") {
      return `source pull request was reopened after ClawSweeper closeout: ${source}`;
    }
  } else {
    if (state === "open") return "";
    if (pending && state === "closed") return "";
    if (state === "closed") {
      return `source pull request closed without a completed ClawSweeper receipt: ${source}`;
    }
  }
  return `authorized source pull request is ${state || "unknown"}`;
}

