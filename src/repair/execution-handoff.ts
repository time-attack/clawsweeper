class SourceCloseCompensationFailure extends Error {
  constructor(message: string) {
    super(message);
    this.name = "SourceCloseCompensationFailure";
  }
}

class SourceCloseCompensationUnverified extends Error {
  constructor(message: string) {
    super(message);
    this.name = "SourceCloseCompensationUnverified";
  }
}

export function runReplacementBoundSourceClose({
  assertSourceOpen,
  assertReplacementOpen,
  closeSource,
  readRecovery,
  checkpointCompensation,
  compensateSource,
  readCompensatedRecovery,
}: {
  assertSourceOpen: () => void;
  assertReplacementOpen: () => void;
  closeSource: () => void;
  readRecovery: () => ReturnType<typeof sourceCloseRecovery>;
  checkpointCompensation: (evidence: SourceCloseEvidence) => void;
  compensateSource: () => void;
  readCompensatedRecovery: () => ReturnType<typeof sourceCloseRecovery>;
}):
  | { status: "closed"; evidence: SourceCloseEvidence }
  | { status: "compensated"; evidence: SourceReopenEvidence; replacement_error: string } {
  assertSourceOpen();
  assertReplacementOpen();
  closeSource();

  let replacementError: unknown = null;
  try {
    assertReplacementOpen();
  } catch (error) {
    replacementError = error;
  }
  const recovery = readRecovery();
  if (recovery.status !== "closed") {
    throw new Error("source pull request close did not persist with trusted actor evidence");
  }
  if (!replacementError) return recovery;

  checkpointCompensation(recovery.evidence);
  try {
    compensateSource();
  } catch (compensationError) {
    throw new SourceCloseCompensationFailure(
      `replacement changed after source close and compensation failed: ${String(
        (compensationError as Error)?.message ?? compensationError,
      )}`,
    );
  }
  try {
    const compensated = readCompensatedRecovery();
    if (compensated.status !== "reopened") {
      throw new SourceCloseCompensationUnverified(
        "source close compensation completed but reopen evidence is not yet durable",
      );
    }
    return {
      status: "compensated",
      evidence: compensated.evidence,
      replacement_error: String((replacementError as Error)?.message ?? replacementError),
    };
  } catch (verificationError) {
    if (verificationError instanceof SourceCloseCompensationUnverified) {
      throw verificationError;
    }
    throw new SourceCloseCompensationUnverified(
      `source close compensation completed but could not be verified: ${String(
        (verificationError as Error)?.message ?? verificationError,
      )}`,
    );
  }
}

function closeSupersededReplacementSources({
  cwd,
  intent,
  publication,
  publicationCheckpoint,
  publicationReceiptPath,
  targetPrNumber,
  checkpointedClosures,
  pendingClosures,
  expectedCloseActor,
}: {
  cwd: string;
  intent: ExecutionIntent;
  publication: PreparedPublication;
  publicationCheckpoint: PublicationReceipt;
  publicationReceiptPath: string;
  targetPrNumber: number;
  checkpointedClosures: ReadonlySet<string>;
  pendingClosures: ReadonlySet<string>;
  expectedCloseActor: string;
}): PublicationReceipt {
  let currentReceipt = publicationCheckpoint;
  let completedClosures = new Set(checkpointedClosures);
  const pendingAttempts = new Set(pendingClosures);
  let preservedReopens = preservedReopenedSourceClosures(publication, currentReceipt, intent);
  let pendingReopenAttempts = pendingSourceReopenClosures(publication, currentReceipt, intent);
  const revisions = new Map(
    sourcePullRevisions(intent).map((revision) => [revision.url, revision]),
  );
  const actions = preparedSourceClosureActions(publication, intent);
  const readLiveRecovery = (revision: SourcePullRevision, attempt: LooseRecord) => {
    const sourcePull = revalidateSourcePullRevision(revision, { allowClosed: true });
    return sourceCloseRecovery({
      attempt,
      state: String(sourcePull.state ?? "").toLowerCase(),
      events: readSourcePullStateEvents(revision.repo, revision.number),
    });
  };

  for (const source of pendingAttempts) {
    if (completedClosures.has(source)) continue;
    const revision = revisions.get(source);
    if (!revision) {
      throw new Error("pending source closeout is missing its authorized source revision");
    }
    const attempt = sourceClosureReceiptState(publication, currentReceipt, intent).attempts.get(
      source,
    );
    if (!attempt) {
      throw new Error("pending source closeout is missing its durable begin checkpoint");
    }
    if (
      normalizeActor(sourceClosureExpectedActor(attempt)) !==
      normalizeActor(requiredCloseActor(expectedCloseActor))
    ) {
      throw new Error("pending source closeout actor changed across workflow attempts");
    }
    const recovery = readLiveRecovery(revision, attempt);
    if (pendingReopenAttempts.has(source)) {
      if (recovery.status === "awaiting_reopen" || recovery.status === "retry") {
        throw new Error("source reopen compensation is waiting for durable actor evidence");
      }
      if (recovery.status === "closed") {
        try {
          runPublicationMutation({
            intent,
            publication,
            checkpointedClosures: new Set([...completedClosures, ...pendingAttempts]),
            targetNumbers: [revision.number],
            mutation: () => {
              const liveSource = revalidateSourcePullRevision(revision, { allowClosed: true });
              const liveRecovery = sourceCloseRecovery({
                attempt,
                state: String(liveSource.state ?? "").toLowerCase(),
                events: readSourcePullStateEvents(revision.repo, revision.number),
              });
              if (liveRecovery.status !== "closed") {
                throw new Error("source reopen compensation lost trusted close identity");
              }
              try {
                run("gh", ["pr", "reopen", String(revision.number), "--repo", intent.target_repo], {
                  cwd,
                });
              } catch (error) {
                throw new SourceCloseCompensationFailure(
                  `checkpointed source reopen compensation failed: ${String(
                    (error as Error)?.message ?? error,
                  )}`,
                );
              }
            },
          });
        } catch (error) {
          if (error instanceof SourceCloseCompensationFailure) {
            currentReceipt = finalizePendingSourceClosureReceipt({
              publication,
              receipt: currentReceipt,
              intent,
              revision,
              readRecovery: () => readLiveRecovery(revision, attempt),
            });
            writeJson(publicationReceiptPath, currentReceipt);
          }
          throw error;
        }
        let compensated: ReturnType<typeof sourceCloseRecovery>;
        try {
          const compensatedSource = revalidateSourcePullRevision(revision, { allowClosed: true });
          compensated = sourceCloseRecovery({
            attempt,
            state: String(compensatedSource.state ?? "").toLowerCase(),
            events: readSourcePullStateEvents(revision.repo, revision.number),
          });
        } catch (error) {
          throw new SourceCloseCompensationUnverified(
            `checkpointed source reopen completed but could not be verified: ${String(
              (error as Error)?.message ?? error,
            )}`,
          );
        }
        if (compensated.status !== "reopened" && compensated.status !== "human_reopened") {
          throw new SourceCloseCompensationUnverified(
            "checkpointed source reopen completed but durable reopen evidence is pending",
          );
        }
        currentReceipt = finalizePendingSourceClosureReceipt({
          publication,
          receipt: currentReceipt,
          intent,
          revision,
          readRecovery: () => readLiveRecovery(revision, attempt),
        });
      } else {
        currentReceipt = finalizePendingSourceClosureReceipt({
          publication,
          receipt: currentReceipt,
          intent,
          revision,
          readRecovery: () => readLiveRecovery(revision, attempt),
        });
      }
      writeJson(publicationReceiptPath, currentReceipt);
      completedClosures = checkpointedSourceClosures(publication, currentReceipt, intent);
      preservedReopens = preservedReopenedSourceClosures(publication, currentReceipt, intent);
      pendingReopenAttempts = pendingSourceReopenClosures(publication, currentReceipt, intent);
      pendingAttempts.delete(source);
      continue;
    }
    if (recovery.status === "awaiting_reopen") {
      throw new Error("source reopen is visible before its durable event evidence");
    }
    if (recovery.status === "retry") continue;
    currentReceipt = finalizePendingSourceClosureReceipt({
      publication,
      receipt: currentReceipt,
      intent,
      revision,
      readRecovery: () => readLiveRecovery(revision, attempt),
    });
    writeJson(publicationReceiptPath, currentReceipt);
    completedClosures = checkpointedSourceClosures(publication, currentReceipt, intent);
    preservedReopens = preservedReopenedSourceClosures(publication, currentReceipt, intent);
    pendingReopenAttempts = pendingSourceReopenClosures(publication, currentReceipt, intent);
    pendingAttempts.delete(source);
  }

  for (const source of completedClosures) {
    const revision = revisions.get(source);
    if (!revision) {
      throw new Error("completed source closeout is missing its authorized source revision");
    }
    const sourcePull = revalidateSourcePullRevision(revision, { allowClosed: true });
    const stateBlock = sourceCloseoutStateBlock({
      source,
      state: String(sourcePull.state ?? "").toLowerCase(),
      completed: true,
    });
    if (stateBlock) throw new Error(stateBlock);
  }
  for (const source of preservedReopens) {
    const revision = revisions.get(source);
    if (!revision) {
      throw new Error("preserved source reopen is missing its authorized source revision");
    }
    const sourcePull = revalidateSourcePullRevision(revision);
    if (String(sourcePull.state ?? "").toLowerCase() !== "open") {
      throw new Error(`preserved reopened source pull request is no longer open: ${source}`);
    }
  }

  ensurePublishedReplacementAvailable({
    cwd,
    intent,
    publication,
    publicationCheckpoint: currentReceipt,
    targetPrNumber,
    checkpointedClosures: completedClosures,
    expectedCloseActor,
  });

  for (const action of actions) {
    if (action.operation === "close") {
      if (preservedReopens.has(action.source.url)) continue;
      if (!completedClosures.has(action.source.url) && !pendingAttempts.has(action.source.url)) {
        throw new Error("source closeout lacks its durable pre-close checkpoint");
      }
      const sourcePull = revalidateSourcePullRevision(action.revision, {
        allowClosed:
          completedClosures.has(action.source.url) || pendingAttempts.has(action.source.url),
      });
      const sourceState = String(sourcePull.state ?? "").toLowerCase();
      const stateBlock = sourceCloseoutStateBlock({
        source: action.source.url,
        state: sourceState,
        completed: completedClosures.has(action.source.url),
        pending: pendingAttempts.has(action.source.url),
      });
      if (stateBlock) throw new Error(stateBlock);
      if (completedClosures.has(action.source.url)) {
        continue;
      }
      if (pendingAttempts.has(action.source.url) && sourceState === "closed") {
        throw new Error("pending source closeout was not checkpointed as completed");
      }
    }
    ensurePublishedReplacementAvailable({
      cwd,
      intent,
      publication,
      publicationCheckpoint: currentReceipt,
      targetPrNumber,
      checkpointedClosures: completedClosures,
      expectedCloseActor,
    });
    const replacementUrl = `https://github.com/${intent.target_repo}/pull/${targetPrNumber}`;
    const comment = [
      "<!-- clawsweeper-replacement-publication -->",
      `ClawSweeper published the independently validated replacement at ${replacementUrl}.`,
      `Validated commit: \`${publication.prepared_head_sha}\``,
    ].join("\n");
    publishExactPullComment({
      checkout: cwd,
      intent,
      publication,
      number: action.source.number,
      body: comment,
      targetNumbers: [targetPrNumber],
      checkpointedClosures: completedClosures,
    });
    if (action.operation !== "close") continue;

    const attempt = sourceClosureReceiptState(publication, currentReceipt, intent).attempts.get(
      action.source.url,
    );
    if (!attempt) throw new Error("source closeout lacks its durable begin checkpoint");
    const authorizedClosedSources = new Set([...completedClosures, ...pendingAttempts]);
    let result: ReturnType<typeof runReplacementBoundSourceClose> | undefined;
    try {
      result = runPublicationMutation({
        intent,
        publication,
        checkpointedClosures: authorizedClosedSources,
        targetNumbers: [targetPrNumber, action.source.number],
        mutation: () =>
          runReplacementBoundSourceClose({
            assertSourceOpen: () => {
              const sourcePull = revalidateSourcePullRevision(action.revision);
              if (String(sourcePull.state ?? "").toLowerCase() !== "open") {
                throw new Error("authorized source pull request is no longer open");
              }
            },
            assertReplacementOpen: () =>
              assertExactPublishedReplacementAvailable({
                intent,
                publication,
                publicationCheckpoint: currentReceipt,
                targetPrNumber,
              }),
            closeSource: () => {
              run(
                "gh",
                ["pr", "close", String(action.source.number), "--repo", intent.target_repo],
                { cwd },
              );
            },
            readRecovery: () => {
              const sourcePull = revalidateSourcePullRevision(action.revision, {
                allowClosed: true,
              });
              return sourceCloseRecovery({
                attempt,
                state: String(sourcePull.state ?? "").toLowerCase(),
                events: readSourcePullStateEvents(action.revision.repo, action.revision.number),
              });
            },
            checkpointCompensation: (evidence) => {
              currentReceipt = checkpointPendingSourceReopenReceipt({
                publication,
                receipt: currentReceipt,
                intent,
                revision: action.revision,
                evidence,
              });
              writeJson(publicationReceiptPath, currentReceipt);
            },
            compensateSource: () =>
              runPublicationMutation({
                intent,
                publication,
                checkpointedClosures: authorizedClosedSources,
                targetNumbers: [action.source.number],
                mutation: () => {
                  const sourcePull = revalidateSourcePullRevision(action.revision, {
                    allowClosed: true,
                  });
                  const recovery = sourceCloseRecovery({
                    attempt,
                    state: String(sourcePull.state ?? "").toLowerCase(),
                    events: readSourcePullStateEvents(action.revision.repo, action.revision.number),
                  });
                  if (recovery.status !== "closed") {
                    throw new Error("source close compensation lost trusted close identity");
                  }
                  run(
                    "gh",
                    ["pr", "reopen", String(action.source.number), "--repo", intent.target_repo],
                    { cwd },
                  );
                },
              }),
            readCompensatedRecovery: () => {
              const sourcePull = revalidateSourcePullRevision(action.revision, {
                allowClosed: true,
              });
              return sourceCloseRecovery({
                attempt,
                state: String(sourcePull.state ?? "").toLowerCase(),
                events: readSourcePullStateEvents(action.revision.repo, action.revision.number),
              });
            },
          }),
      });
    } catch (error) {
      if (error instanceof SourceCloseCompensationFailure) {
        currentReceipt = finalizePendingSourceClosureReceipt({
          publication,
          receipt: currentReceipt,
          intent,
          revision: action.revision,
          readRecovery: () => readLiveRecovery(action.revision, attempt),
        });
        writeJson(publicationReceiptPath, currentReceipt);
      }
      throw error;
    }
    if (!result) throw new Error("source close mutation returned no result");
    if (result.status === "compensated") {
      currentReceipt = finalizePendingSourceClosureReceipt({
        publication,
        receipt: currentReceipt,
        intent,
        revision: action.revision,
        readRecovery: () => readLiveRecovery(action.revision, attempt),
      });
      writeJson(publicationReceiptPath, currentReceipt);
      throw new Error(
        `replacement changed after source close; source reopen compensation persisted: ${result.replacement_error}`,
      );
    }
    currentReceipt = finalizePendingSourceClosureReceipt({
      publication,
      receipt: currentReceipt,
      intent,
      revision: action.revision,
      readRecovery: () => readLiveRecovery(action.revision, attempt),
    });
    writeJson(publicationReceiptPath, currentReceipt);
    completedClosures = checkpointedSourceClosures(publication, currentReceipt, intent);
    pendingAttempts.delete(action.source.url);
  }
  return currentReceipt;
}

function preparedSourceClosureActions(
  publication: PreparedPublication,
  intent: ExecutionIntent,
): Array<{
  source: NonNullable<ReturnType<typeof parsePullRequestUrl>>;
  revision: SourcePullRevision;
  operation: string;
}> {
  const revisions = new Map(
    sourcePullRevisions(intent).map((revision) => [revision.url, revision]),
  );
  return publication.superseded_source_actions.map((action) => {
    const source = parsePullRequestUrl(action.source);
    if (!source || source.repo !== intent.target_repo || !intent.source_prs.includes(source.url)) {
      throw new Error("prepared source closeout redirected outside the authorized source set");
    }
    if (!["comment", "close"].includes(String(action.operation ?? ""))) {
      throw new Error("prepared source closeout contains an unsupported operation");
    }
    const revision = revisions.get(source.url);
    if (!revision) {
      throw new Error("prepared source closeout is missing its authorized source revision");
    }
    return { source, revision, operation: String(action.operation) };
  });
}

