function publishPreparedRef({
  checkout,
  intent,
  publication,
  targetRef,
  expectedSha,
  targetPrNumber = null,
  checkpointedClosures = new Set(),
}: {
  checkout: string;
  intent: ExecutionIntent;
  publication: PreparedPublication;
  targetRef: string;
  expectedSha: string | null;
  targetPrNumber?: number | null;
  checkpointedClosures?: ReadonlySet<string>;
}) {
  const liveSha = remoteRefSha(checkout, targetRef);
  const state = preparedRefPublicationState({
    liveSha,
    expectedSha,
    preparedSha: publication.prepared_head_sha,
  });
  if (state === "publish") {
    const pushArgs = expectedSha
      ? [
          "push",
          `--force-with-lease=${targetRef}:${expectedSha}`,
          "origin",
          `refs/clawsweeper/prepared:${targetRef}`,
        ]
      : ["push", "origin", `refs/clawsweeper/prepared:${targetRef}`];
    runPublicationMutation({
      intent,
      publication,
      checkpointedClosures,
      targetNumbers: [targetPrNumber],
      mutation: () => run("git", pushArgs, { cwd: checkout }),
    });
  }
  if (remoteRefSha(checkout, targetRef) !== publication.prepared_head_sha) {
    throw new Error("published branch does not match the exact validated repair commit");
  }
}

function remoteRefSha(checkout: string, ref: string): string | null {
  const output = run("git", ["ls-remote", "--refs", "origin", ref], { cwd: checkout }).trim();
  if (!output) return null;
  const lines = output.split("\n").filter(Boolean);
  if (lines.length !== 1) throw new Error(`remote ref ${ref} is ambiguous`);
  const [sha, remoteRef] = lines[0]!.split(/\s+/, 2);
  if (remoteRef !== ref || !sha || !SHA_PATTERN.test(sha)) {
    throw new Error(`remote ref ${ref} returned an invalid commit`);
  }
  return sha.toLowerCase();
}

export function runPublicationMutationBoundary<T>({
  intent,
  publication,
  targetNumbers,
  checkpointedClosures = new Set(),
  readers = {},
  mutation,
}: {
  intent: ExecutionIntent;
  publication: PreparedPublication;
  targetNumbers: ReadonlyArray<number | null>;
  checkpointedClosures?: ReadonlySet<string>;
  readers?: PublicationSourceIdentityReaders;
  mutation: () => T;
}): T {
  const readIssue =
    readers.readIssue ??
    ((repo: string, number: number) => ghObject(`repos/${repo}/issues/${number}`));
  const readComments =
    readers.readComments ??
    ((repo: string, number: number) =>
      ghPagedArray(`repos/${repo}/issues/${number}/comments?per_page=100`));
  assertPublicationSourceIdentity({ intent, publication, checkpointedClosures, readers });
  assertPublicationSafetyBoundary(intent, targetNumbers, (repo, number) => ({
    labels: githubLabelNames(readIssue(repo, number).labels),
    comments: readComments(repo, number).map((comment) => String(comment.body ?? "")),
  }));
  return mutation();
}

function runPublicationMutation<T>(
  options: Omit<Parameters<typeof runPublicationMutationBoundary<T>>[0], "readers">,
): T {
  return runPublicationMutationBoundary(options);
}

export function publicationPauseItems(
  intent: ExecutionIntent,
  targetNumbers: ReadonlyArray<number | null> = [],
): Array<{ repo: string; number: number }> {
  const items = new Map<string, { repo: string; number: number }>();
  const add = (repo: string, number: number | null) => {
    if (!Number.isInteger(number) || Number(number) <= 0) return;
    items.set(`${repo.toLowerCase()}#${number}`, { repo, number: Number(number) });
  };
  add(intent.source.repo, intent.source.number);
  for (const sourceUrl of intent.source_prs) {
    const source = parsePullRequestUrl(sourceUrl);
    if (!source || source.repo !== intent.target_repo || source.url !== sourceUrl) {
      throw new Error("execution intent contains an invalid publication source PR");
    }
    add(source.repo, source.number);
  }
  for (const targetNumber of targetNumbers) add(intent.target_repo, targetNumber);
  return [...items.values()];
}

